<div class="howto-step">
  <h2>Быстрый доступ и управление вашей коллекцией</h2>

  <p>
    Чтобы мгновенно открыть вашу коллекцию, просто <strong>нажмите на иконку расширения</strong> в правом верхнем углу браузера. Вы сразу попадёте в окно вашей персональной коллекции.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Открыть всплывающее окно расширения">
  </div>

  <p>
    Во вкладке <strong>Коллекция</strong> вы можете <strong>включить или отключить расширение</strong> одним нажатием. Это позволит быстро вернуть стандартный курсор системы при необходимости.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Включение или отключение расширения">
  </div>

  <p>
    Вы также можете <strong>включить визуальные эффекты</strong> для курсора. Просто включите эффект, и он применится мгновенно.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Включить эффекты">
  </div>
