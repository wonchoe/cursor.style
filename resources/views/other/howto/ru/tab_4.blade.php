<h2>Как использовать курсоры в расширении?</h2>
<p>
  После того как вы добавили несколько курсоров через основное меню расширения, вы можете захотеть управлять ими.
  Удалить курсор из активного списка просто: наведите на него курсор мыши и нажмите на <strong>иконку корзины</strong>, которая появится сверху. Это сразу удалит курсор из видимого списка, сохранив порядок и удобство.
</p>

<img src="/images/en/howto/delete.png" alt="Удалить курсор" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

<p>
  Если вы хотите изменить эффект, связанный с курсором, просто выберите его из выпадающего списка и нажмите кнопку
  <strong>«Посмотреть дополнительные настройки»</strong>. Здесь вы сможете точно настроить внешний вид и поведение эффекта под ваш вкус.
</p>

