<div class="howto-step">
  <h2>Cómo aplicar un cursor desde tu colección</h2>

  <p>
    Para usar un cursor en la extensión, primero debes <strong>agregarlo a tu colección</strong>.
    Puedes hacerlo haciendo clic en el botón “Agregar a la colección” debajo de la vista previa del cursor. Esto añadirá el cursor seleccionado a tu lista personal dentro de la extensión.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Agregar a la colección">
  </div>

  <p>
    Una vez agregado el cursor, abre tu colección dentro de la extensión. Para <strong>aplicar el cursor</strong>, solo haz clic sobre él — y se convertirá instantáneamente en tu cursor activo.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Aplicar cursor">
  </div>
</div>
