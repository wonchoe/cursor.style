<div class="howto-step">
  <h2>Accede y gestiona tu colección rápidamente</h2>

  <p>
    Para abrir tu colección al instante, simplemente <strong>haz clic en el icono de la extensión</strong> en la esquina superior derecha del navegador. Esto te llevará directamente a tu ventana de colección personal.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Abrir extensión emergente">
  </div>

  <p>
    Dentro de la pestaña <strong>Colección</strong>, puedes <strong>activar o desactivar la extensión</strong> fácilmente con un solo clic. Esto te permite volver rápidamente al cursor predeterminado del sistema cuando lo necesites.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Activar o desactivar extensión">
  </div>

  <p>
    También puedes <strong>activar efectos visuales</strong> para tu cursor. Simplemente activa el interruptor de efectos y tu mejora visual seleccionada se aplicará al instante.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Activar efectos">
  </div>
</div>
