<h2>¿Cómo usar los cursores dentro de la extensión?</h2>
<p>
  Después de agregar algunos cursores a tu lista mediante la interfaz principal de la extensión, es posible que desees gestionarlos.
  Eliminar un cursor de tu lista activa es fácil: simplemente pasa el cursor sobre el que ya no necesitas y haz clic en el
  <strong>ícono de papelera</strong> que aparece encima. Esto eliminará el cursor de inmediato, manteniendo tu espacio de trabajo limpio y organizado.
</p>

<img src="/images/en/howto/delete.png" alt="Eliminar cursor" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

<p>
  Si deseas cambiar el efecto asociado con un cursor, simplemente selecciónalo en el menú desplegable y haz clic en el botón
  <strong>"Ver opciones adicionales"</strong>. Allí podrás ajustar cómo se ve y se comporta el efecto para adaptarlo mejor a tu estilo.
</p>

