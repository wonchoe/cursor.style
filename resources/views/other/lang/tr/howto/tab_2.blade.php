  <h2>Koleksiyonunuza hızlıca erişin ve yönetin</h2>

  <p>
    Koleksiyonunuzu anında açmak için, tarayıcınızın sağ üst köşesindeki <strong>uzantı simgesine tıklamanız</strong> yeterlidir. Bu sizi doğrudan kişisel koleksiyon pencerenize götürecektir.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Uzantı açılır penceresini aç">
  </div>

  <p>
    <strong>Koleksiyon</strong> sekmesinin içinde, uzantıyı tek tıkla kolayca <strong>etkinleştirebilir veya devre dışı bırakabilirsiniz</strong>. Bu, gerektiğinde varsayılan sistem imlecini hızla geri getirmenize yardımcı olur.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Uzantıyı etkinleştirin veya devre dışı bırakın">
  </div>

  <p>
    Ayrıca imleciniz için <strong>görsel efektleri etkinleştirebilirsiniz</strong>. Efekt anahtarını değiştirmeniz yeterlidir; seçtiğiniz görsel geliştirme anında uygulanacaktır.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Efektleri açın">
  </div>
</div>
