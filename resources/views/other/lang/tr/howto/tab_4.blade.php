<h2>Uzantı içinde imleçler nasıl kullanılır?</h2>
  <p>
    Ana uzantı arayüzünü kullanarak listenize birkaç imleç ekledikten sonra, bunları yönetmek isteyebilirsiniz.
    Etkin listenizden bir imleci kaldırmak basittir: artık ihtiyacınız olmayan imlecin üzerine gelin ve üzerine tıklayın.
    <strong>çöp kutusu simgesi</strong> görünür. Bu işlem, imleci görünür listenizden hemen siler, çalışma alanınızın temiz ve düzenli kalmasını sağlar.
  </p>

  <img src="/images/tr/howto/delete.png" alt="İmleci sil" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Bir imleçle ilişkilendirilen efekti değiştirmek isterseniz, onu açılır seçiciden seçmeniz ve
    <strong>"Ek seçenekleri görüntüle"</strong> düğmesine tıklamanız yeterlidir. Orada efektin tarzınıza daha uygun görünmesini ve davranmasını ince ayarlayabilirsiniz.
  </p>

