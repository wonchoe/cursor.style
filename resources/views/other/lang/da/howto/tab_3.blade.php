<div class="howto-step">
  <h2>Sådan anvender du en markør fra din samling</h2>

  <p>
    For at bruge en markør i udvidelsen, skal du først <strong>føje den til din samling</strong>.
    Du kan gøre dette ved at klikke på knappen “Føj til samling” under markørforhåndsvisningen. Dette tilføjer den valgte markør til din personlige liste inde i udvidelsen.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Føj til samling">
  </div>

  <p>
    Når markøren er blevet tilføjet, skal du åbne din samling inde i udvidelsen. For at <strong>anvende markøren</strong>, skal du bare klikke på den – og den bliver din aktive markør med det samme.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Anvend markør">
  </div>
</div>
