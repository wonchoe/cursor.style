
  <h2>Hvordan bruger man cursors i udvidelsen?</h2>
  <p>
    Når du har tilføjet et par cursors til din liste ved hjælp af hovedgrænsefladen i udvidelsen, vil du måske gerne administrere dem.
    Det er nemt at fjerne en cursor fra din aktive liste: bare hold musen over den cursor, du ikke længere har brug for, og klik på
    <strong>papirkurvsikonet</strong>, der vises oven på den. Denne handling sletter straks cursoren fra din
    synlige liste, så dit arbejdsområde er rent og organiseret.
  </p>

  <img src="/images/en/howto/delete.png" alt="Slet cursor" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Hvis du vil ændre den effekt, der er knyttet til en cursor, skal du blot vælge den fra rullelisten og klikke på
    knappen <strong>"Vis yderligere indstillinger"</strong>. Der kan du finjustere, hvordan effekten ser ud og opfører sig, så den passer bedre til din stil.
  </p>

