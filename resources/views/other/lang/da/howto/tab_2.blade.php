<div class="howto-step">
  <h2>Få hurtig adgang til og administrer din samling</h2>

  <p>
    For øjeblikkeligt at åbne din samling skal du bare <strong>klikke på udvidelsesikonet</strong> i øverste højre hjørne af din browser. Dette vil føre dig direkte til dit personlige samlingsvindue.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Åbn udvidelsespopup">
  </div>

  <p>
    Inden i fanen <strong>Samling</strong> kan du nemt <strong>aktivere eller deaktivere udvidelsen</strong> med et enkelt klik. Dette hjælper dig med hurtigt at skifte tilbage til standard systemmarkøren, når det er nødvendigt.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Aktiver eller deaktiver udvidelse">
  </div>

  <p>
    Du kan også <strong>aktivere visuelle effekter</strong> for din markør. Bare slå effektkontakten til, og din valgte visuelle forbedring vil blive anvendt med det samme.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Slå effekter til">
  </div>
</div>
