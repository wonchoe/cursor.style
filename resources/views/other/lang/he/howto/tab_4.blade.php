 
  <h2>איך להשתמש בסמנים בתוך התוסף?</h2>
  <p>
    אחרי שהוספת כמה סמנים לרשימה שלך באמצעות ממשק התוסף הראשי, ייתכן שתרצה לנהל אותם.
    הסרה של סמן מהרשימה הפעילה שלך היא פשוטה: פשוט רחף מעל לסמן שאינך זקוק לו עוד, ולחץ על
    <strong>סמל פח האשפה</strong> שמופיע מעליו. פעולה זו מוחקת מיידית את הסמן מה
    רשימה הנראית לעין שלך, ושומרת על סביבת העבודה שלך נקייה ומסודרת.
  </p>

  <img src="/images/en/howto/delete.png" alt="מחק סמן" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    אם ברצונך לשנות את האפקט המשויך לסמן, פשוט בחר אותו מבורר הנפתח ולחץ
    על כפתור <strong>"הצג אפשרויות נוספות"</strong>. שם תוכל לכוונן את מראה האפקט ואת אופן פעולתו כדי שיתאים טוב יותר לסגנון שלך.
  </p>

