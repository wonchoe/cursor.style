<div class="howto-step">
  <h2>Cum să aplici un cursor din colecția ta</h2>

  <p>
    Pentru a utiliza un cursor în extensie, trebuie mai întâi să îl <strong>adaugi în colecția ta</strong>.
    Poți face asta dând clic pe butonul "Adaugă în colecție" sub previzualizarea cursorului. Acest lucru adaugă cursorul selectat la lista ta personală din interiorul extensiei.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Adaugă în colecție">
  </div>

  <p>
    Odată ce cursorul a fost adăugat, deschide colecția ta în interiorul extensiei. Pentru a <strong>aplica cursorul</strong>, doar dă clic pe el – și acesta va deveni cursorul tău activ instantaneu.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Aplică cursor">
  </div>
</div>
