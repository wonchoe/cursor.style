<div class="howto-step">
  <h2>Accesați și gestionați-vă colecția rapid</h2>

  <p>
    Pentru a deschide instantaneu colecția, doar <strong>faceți clic pe pictograma extensiei</strong> din colțul din dreapta sus al browserului. Aceasta vă va duce direct la fereastra colecției personale.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Deschideți fereastra pop-up a extensiei">
  </div>

  <p>
    În interiorul tab-ului <strong>Colecție</strong>, puteți <strong>activa sau dezactiva cu ușurință extensia</strong>, cu un singur clic. Acest lucru vă ajută să reveniți rapid la cursorul implicit al sistemului, atunci când este necesar.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Activați sau dezactivați extensia">
  </div>

  <p>
    De asemenea, puteți <strong>activa efecte vizuale</strong> pentru cursorul dvs.. Doar comutați butonul de efect și îmbunătățirea vizuală selectată va fi aplicată instantaneu.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Activați efecte">
  </div>
</div>
