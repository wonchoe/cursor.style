<div class="howto-step">
  <h2>Akses dan urus koleksi anda dengan pantas</h2>

  <p>
    Untuk membuka koleksi anda dengan serta-merta, cuma <strong>klik ikon sambungan</strong> di sudut kanan atas pelayar anda. Ini akan membawa anda terus ke tetingkap koleksi peribadi anda.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Buka popup sambungan">
  </div>

  <p>
    Di dalam tab <strong>Koleksi</strong>, anda boleh dengan mudah <strong>mendayakan atau mematikan sambungan</strong> dengan satu klik. Ini membantu anda dengan cepat kembali ke kursor sistem lalai apabila diperlukan.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Dayakan atau lumpuhkan sambungan">
  </div>

  <p>
    Anda juga boleh <strong>mendayakan kesan visual</strong> untuk kursor anda. Cuma tukar suis kesan, dan peningkatan visual yang anda pilih akan digunakan serta-merta.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Hidupkan kesan">
  </div>
