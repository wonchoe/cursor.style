<div class="howto-step">
  <h2>Cara untuk menggunakan kursor daripada koleksi anda</h2>

  <p>
    Untuk menggunakan kursor dalam sambungan, anda perlu <strong>menambahnya ke koleksi anda</strong> terlebih dahulu.
    Anda boleh melakukan ini dengan mengklik butang “Tambah ke Koleksi” di bawah pratonton kursor. Ini menambahkan kursor yang dipilih ke senarai peribadi anda di dalam sambungan.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Tambah ke koleksi">
  </div>

  <p>
    Setelah kursor ditambahkan, buka koleksi anda di dalam sambungan. Untuk <strong>menggunakan kursor</strong>, hanya klik padanya – dan ia akan menjadi kursor aktif anda serta-merta.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Gunakan kursor">
  </div>
</div>
