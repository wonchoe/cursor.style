  <h2>Come usare i cursori all'interno dell'estensione?</h2>
  <p>
    Dopo aver aggiunto alcuni cursori alla lista usando l'interfaccia principale dell'estensione, potresti volerli gestire.
    Rimuovere un cursore dalla tua lista attiva è semplice: basta passare il mouse sopra il cursore di cui non hai più bisogno e cliccare l'icona del
    <strong>cestino</strong> che appare sopra di esso. Questa azione elimina immediatamente il cursore dalla tua
    lista visibile, mantenendo il tuo spazio di lavoro pulito e organizzato.
  </p>

  <img src="/images/en/howto/delete.png" alt="Cancella cursore" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Se vuoi cambiare l'effetto associato a un cursore, selezionalo semplicemente dal selettore a tendina e clicca
    il pulsante <strong>"Visualizza opzioni aggiuntive"</strong>. Lì puoi regolare come l'effetto appare e si comporta per adattarsi meglio al tuo stile.
  </p>

