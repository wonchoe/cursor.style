<div class="howto-step">
  <h2>Accedi e gestisci velocemente la tua collezione</h2>

  <p>
    Per aprire istantaneamente la tua collezione, è sufficiente <strong>cliccare sull'icona dell'estensione</strong> nell'angolo in alto a destra del tuo browser. Questo ti porterà direttamente alla finestra della tua collezione personale.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Apri il popup dell'estensione">
  </div>

  <p>
    All'interno della scheda <strong>Collection</strong>, puoi facilmente <strong>abilitare o disabilitare l'estensione</strong> con un solo click. Questo ti aiuta a tornare rapidamente al cursore di sistema predefinito quando necessario.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Abilita o disabilita l'estensione">
  </div>

  <p>
    Puoi anche <strong>abilitare effetti visivi</strong> per il tuo cursore. Basta attivare l'interruttore degli effetti e il tuo miglioramento visivo selezionato verrà applicato istantaneamente.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Attiva gli effetti">
  </div>
</div>
