
  <h2>Kako koristiti kursore unutar ekstenzije?</h2>
  <p>
    Nakon što ste dodali nekoliko kursora na vašu listu koristeći glavni interfejs ekstenzije, možda želite da ih upravljate.
    Uklanjanje kursora sa vaše aktivne liste je jednostavno: samo pređite mišem preko kursora koji vam više nije potreban i kliknite na
    <strong>ikonicu kante za smeće</strong> koja se pojavi na vrhu. Ova akcija odmah briše kursor sa vaše
    vidljive liste, čineći vaš radni prostor čistim i organizovanim.
  </p>

  <img src="/images/en/howto/delete.png" alt="Obriši kursor" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Ako želite da promenite efekat povezan sa kursorom, jednostavno ga izaberite iz padajućeg menija i kliknite na
    dugme <strong>"Prikaži dodatne opcije"</strong>. Tamo možete fino podesiti kako efekat izgleda i kako se ponaša kako bi bolje odgovarao vašem stilu.
  </p>

