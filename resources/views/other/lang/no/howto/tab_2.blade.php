<div class="howto-step">
  <h2>Få rask tilgang til og administrer samlingen din</h2>

  <p>
    For å umiddelbart åpne samlingen din, bare <strong>klikk på utvidelsesikonet</strong> i øvre høyre hjørne av nettleseren din. Dette vil ta deg direkte til ditt personlige samlingsvindu.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Åpne utvidelsens popup">
  </div>

  <p>
    Inne i <strong>Samling</strong>-fanen kan du enkelt <strong>aktivere eller deaktivere utvidelsen</strong> med ett klikk. Dette hjelper deg med raskt å bytte tilbake til standard systemmarkør når det trengs.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Aktiver eller deaktiver utvidelse">
  </div>

  <p>
    Du kan også <strong>aktivere visuelle effekter</strong> for markøren din. Bare slå effektbryteren på, så vil din valgte visuelle forbedring bli brukt umiddelbart.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Slå på effekter">
  </div>
</div>
