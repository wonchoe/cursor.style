<div class="howto-step">
  <h2>如何从您的收藏应用光标</h2>

  <p>
    要在扩展程序中使用光标，您首先需要<strong>将其添加到您的收藏</strong>。
    您可以通过点击光标预览下方的“添加到收藏”按钮来执行此操作。这会将所选光标添加到扩展程序内的您的个人列表中。
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="添加到收藏">
  </div>

  <p>
    添加光标后，在扩展程序内打开您的收藏。要<strong>应用光标</strong>，只需单击它 - 它将立即成为您的活动光标。
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="应用光标">
  </div>
</div>
