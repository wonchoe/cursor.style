<div class="howto-step">
  <h2>Snel toegang tot en beheer uw verzameling</h2>

  <p>
    Om uw verzameling direct te openen, <strong>klik je gewoon op het extensie-icoon</strong> in de rechterbovenhoek van uw browser. Dit brengt u direct naar uw persoonlijke verzamelingsvenster.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Open extension popup">
  </div>

  <p>
    In het <strong>Verzameling</strong>-tabblad kunt u de <strong>extensie eenvoudig in- of uitschakelen</strong> met één klik. Dit helpt u snel terug te schakelen naar de standaard systeemcursor wanneer dat nodig is.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Enable or disable extension">
  </div>

  <p>
    U kunt ook <strong>visuele effecten inschakelen</strong> voor uw cursor. Schakel gewoon de effect-schakelaar om en uw geselecteerde visuele verbetering wordt direct toegepast.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Turn on effects">
  </div>
</div>