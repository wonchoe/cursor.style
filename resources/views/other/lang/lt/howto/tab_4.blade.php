
  <h2>Kaip naudoti kursorius plėtinio viduje?</h2>
  <p>
    Pridėję keletą kursorų prie savo sąrašo naudodami pagrindinę plėtinio sąsają, galbūt norėsite juos tvarkyti.
    Ištrinti kursorį iš savo aktyvaus sąrašo yra paprasta: tiesiog užveskite pelės žymeklį ant jums nebereikalingo kursoriaus ir spustelėkite
    <strong>šiukšliadėžės piktogramą</strong>, kuri pasirodo jo viršuje. Šis veiksmas nedelsiant ištrina kursorį iš jūsų
    matomo sąrašo, išlaikydamas jūsų darbo vietą švarią ir tvarkingą.
  </p>

  <img src="/images/en/howto/delete.png" alt="Ištrinti kursorį" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Jei norite pakeisti su kursoriaus susietą efektą, tiesiog pasirinkite jį iš išskleidžiamojo sąrašo ir spustelėkite
    mygtuką <strong>"Peržiūrėti papildomas parinktis"</strong>. Ten galite tiksliai sureguliuoti, kaip efektas atrodo ir veikia, kad jis geriau atitiktų jūsų stilių.
  </p>

