
  <h2>Kako koristiti pokazivače unutar ekstenzije?</h2>
  <p>
    Nakon što ste dodali nekoliko pokazivača na svoj popis pomoću glavnog sučelja ekstenzije, možda ćete ih htjeti upravljati njima.
    Uklanjanje pokazivača s aktivnog popisa je jednostavno: samo pređite mišem iznad pokazivača koji više ne trebate i kliknite
    <strong>ikonu kante za smeće</strong> koja se pojavljuje iznad njega. Ova radnja odmah briše pokazivač s vašeg
    vidljivog popisa, održavajući vaš radni prostor čistim i organiziranim.
  </p>

  <img src="/images/en/howto/delete.png" alt="Izbriši pokazivač" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Ako želite promijeniti efekt povezan s pokazivačem, jednostavno ga odaberite iz padajućeg izbornika i kliknite na
    gumb <strong>"Prikaži dodatne opcije"</strong>. Tamo možete fino podesiti kako efekt izgleda i ponaša se kako bi bolje odgovarao vašem stilu.
  </p>

