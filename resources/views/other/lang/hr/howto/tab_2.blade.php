  <h2>Brz pristup i upravljanje vašom kolekcijom</h2>

  <p>
    Da biste odmah otvorili svoju kolekciju, samo <strong>kliknite na ikonu ekstenzije</strong> u gornjem desnom kutu vašeg preglednika. To će vas odvesti izravno u prozor vaše osobne kolekcije.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Otvorite skočni prozor ekstenzije">
  </div>

  <p>
    Unutar kartice <strong>Kolekcija</strong>, možete jednostavno <strong>omogućiti ili onemogućiti ekstenziju</strong> jednim klikom. To vam pomaže da se brzo vratite na zadani sistemski kursor kada je to potrebno.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Omogućite ili onemogućite ekstenziju">
  </div>

  <p>
    Također možete <strong>omogućiti vizualne efekte</strong> za vaš kursor. Samo prebacite prekidač za efekt i vaše odabrano vizualno poboljšanje će se odmah primijeniti.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Uključite efekte">
  </div>
