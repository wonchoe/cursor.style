<div class="howto-step">
  <h2>Kako primijeniti kursor iz svoje kolekcije</h2>

  <p>
    Da biste koristili kursor u proširenju, prvo ga morate <strong>dodati u svoju kolekciju</strong>.
    To možete učiniti klikom na gumb "Dodaj u kolekciju" ispod pregleda kursora. Time se odabrani kursor dodaje na vaš osobni popis unutar proširenja.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Dodaj u kolekciju">
  </div>

  <p>
    Nakon što je kursor dodan, otvorite svoju kolekciju unutar proširenja. Da biste <strong>primijenili kursor</strong>, samo kliknite na njega – i on će odmah postati vaš aktivni kursor.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Primijeni kursor">
  </div>
