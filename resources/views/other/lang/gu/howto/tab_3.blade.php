  <h2>તમારા કલેક્શનમાંથી કર્સર કેવી રીતે લાગુ કરવું</h2>

  <p>
    એક્સ્ટેંશનમાં કર્સરનો ઉપયોગ કરવા માટે, તમારે પહેલા તેને <strong>તમારા કલેક્શનમાં ઉમેરવાની</strong> જરૂર છે.
    તમે કર્સર પ્રીવ્યૂની નીચે “કલેક્શનમાં ઉમેરો” બટન પર ક્લિક કરીને આ કરી શકો છો. આ પસંદ કરેલ કર્સરને એક્સ્ટેંશનની અંદર તમારી વ્યક્તિગત સૂચિમાં ઉમેરે છે.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="કલેક્શનમાં ઉમેરો">
  </div>

  <p>
    એકવાર કર્સર ઉમેરાયા પછી, એક્સ્ટેંશનની અંદર તમારું કલેક્શન ખોલો. <strong>કર્સરને લાગુ કરવા માટે</strong>, ફક્ત તેના પર ક્લિક કરો – અને તે તરત જ તમારું સક્રિય કર્સર બની જશે.
  </p>

