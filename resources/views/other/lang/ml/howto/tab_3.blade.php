  <h2>നിങ്ങളുടെ ശേഖരത്തിൽ നിന്ന് ഒരു കേഴ്സർ എങ്ങനെ പ്രയോഗിക്കാം</h2>

  <p>
    എക്സ്റ്റൻഷനിൽ ഒരു കേഴ്സർ ഉപയോഗിക്കുന്നതിന്, നിങ്ങൾ ആദ്യം അത് <strong>നിങ്ങളുടെ ശേഖരത്തിലേക്ക് ചേർക്കണം</strong>.
    കേഴ്സർ പ്രിവ്യൂവിന്റെ കീഴിലുള്ള “ശേഖരത്തിലേക്ക് ചേർക്കുക” ബട്ടണിൽ ക്ലിക്കുചെയ്ത് നിങ്ങൾക്ക് ഇത് ചെയ്യാൻ കഴിയും. ഇത് തിരഞ്ഞെടുത്ത കേഴ്സറിനെ എക്സ്റ്റൻഷനുള്ളിലെ നിങ്ങളുടെ വ്യക്തിഗത ലിസ്റ്റിലേക്ക് ചേർക്കുന്നു.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="ശേഖരത്തിലേക്ക് ചേർക്കുക">
  </div>

  <p>
    കേഴ്സർ ചേർത്തുകഴിഞ്ഞാൽ, എക്സ്റ്റൻഷനുള്ളിൽ നിങ്ങളുടെ ശേഖരം തുറക്കുക. <strong>കേഴ്സർ പ്രയോഗിക്കുന്നതിന്</strong>, അതിൽ ക്ലിക്കുചെയ്യുക – അത് തൽക്ഷണം നിങ്ങളുടെ സജീവ കേഴ്സറാകും.
  </p>

