  <p>
    ഒരു കേഴ്​സറുമായി ബന്ധപ്പെട്ട ഇഫക്റ്റ് മാറ്റണമെങ്കിൽ, ഡ്രോപ്പ്‌ഡൗൺ സെലക്ടറിൽ നിന്ന് അത് തിരഞ്ഞെടുത്ത് <strong>"കൂടുതൽ ഓപ്ഷനുകൾ കാണുക"</strong> ബട്ടൺ ക്ലിക്ക് ചെയ്യുക.
    അവിടെ നിങ്ങളുടെ ശൈലിക്ക് അനുയോജ്യമായ രീതിയിൽ ഇഫക്റ്റ് എങ്ങനെ കാണപ്പെടുന്നു എന്നും എങ്ങനെ പ്രവർത്തിക്കുമെന്നും നിങ്ങൾക്ക് നന്നായി ക്രമീകരിക്കാൻ കഴിയും.
  </p>

