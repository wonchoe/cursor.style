<h2>എക്സ്റ്റൻഷൻ്റെ ഉള്ളിൽ എങ്ങനെ കേഴ്​സറുകൾ ഉപയോഗിക്കാം?</h2>
  <p>
    പ്രധാന എക്സ്റ്റൻഷൻ ഇന്റർഫേസ് ഉപയോഗിച്ച് നിങ്ങളുടെ ലിസ്റ്റിലേക്ക് കുറച്ച് കേഴ്​സറുകൾ ചേർത്ത ശേഷം, നിങ്ങൾക്ക് അവ കൈകാര്യം ചെയ്യാൻ കഴിയും.
    നിങ്ങളുടെ സജീവമായ ലിസ്റ്റിൽ നിന്ന് ഒരു കേഴ്​സർ നീക്കംചെയ്യുന്നത് വളരെ ലളിതമാണ്: നിങ്ങൾക്ക് ഇനി ആവശ്യമില്ലാത്ത കേഴ്​സറിന് മുകളിൽ നിങ്ങളുടെ മൗസ് പോയിന്റർ നീക്കുക, തുടർന്ന് അതിന് മുകളിൽ കാണുന്ന
    <strong>ചവറ്റുകുട്ടയുടെ ഐക്കൺ</strong> ക്ലിക്ക് ചെയ്യുക. ഈ പ്രവർത്തനം നിങ്ങളുടെ ദൃശ്യമായ ലിസ്റ്റിൽ നിന്ന് കേഴ്​സറിനെ ഉടനടി ഇല്ലാതാക്കുന്നു, അതുവഴി നിങ്ങളുടെ വർക്ക്സ്പേസ് വൃത്തിയായി സൂക്ഷിക്കുന്നു.
  </p>

