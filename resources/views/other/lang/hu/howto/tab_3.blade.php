  <h2>Hogyan alkalmazz egy kurzort a gyűjteményedből</h2>

  <p>
    Ahhoz, hogy egy kurzort használj a bővítményben, először <strong>hozzá kell adnod a gyűjteményedhez</strong>.
    Ezt a kurzor előnézete alatt található „Hozzáadás a gyűjteményhez” gombra kattintva teheted meg. Ez hozzáadja a kiválasztott kurzort a személyes listádhoz a bővítményen belül.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Hozzáadás a gyűjteményhez">
  </div>

  <p>
    Miután a kurzor hozzá lett adva, nyisd meg a gyűjteményedet a bővítményen belül. <strong>A kurzor alkalmazásához</strong> kattints rá – és azonnal a te aktív kurzorod lesz.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Kurzor alkalmazása">
  </div>
</div>
