      <img src="/images/en/howto/installextension.png" alt="Kiterjesztés telepítése">
    </a>
  </div>

  <p>
    A telepítés után egy új menüpont, a <strong>"Saját gyűjtemény"</strong> jelenik meg a webhely navigációs sávján. Ez a szakasz lehetővé teszi, hogy egy helyen kezelje az összes kedvenc kurzorát és effektusát.
