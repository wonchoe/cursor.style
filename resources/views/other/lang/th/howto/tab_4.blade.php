
  <h2>วิธีใช้เคอร์เซอร์ภายในส่วนขยาย</h2>
  <p>
    หลังจากที่คุณเพิ่มเคอร์เซอร์สองสามตัวในรายการของคุณโดยใช้ส่วนต่อประสานส่วนขยายหลัก คุณอาจต้องการจัดการเคอร์เซอร์เหล่านั้น
    การลบเคอร์เซอร์ออกจากรายการที่ใช้งานอยู่ทำได้ง่าย ๆ เพียงเลื่อนเมาส์ไปเหนือเคอร์เซอร์ที่คุณไม่ต้องการอีกต่อไป แล้วคลิกที่
    <strong>ไอคอนถังขยะ</strong> ที่ปรากฏด้านบน การกระทำนี้จะลบเคอร์เซอร์ออกจาก
    รายการที่คุณมองเห็นทันที ทำให้พื้นที่ทำงานของคุณสะอาดและเป็นระเบียบ
  </p>

  <img src="/images/en/howto/delete.png" alt="ลบเคอร์เซอร์" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    หากคุณต้องการเปลี่ยนเอฟเฟกต์ที่เกี่ยวข้องกับเคอร์เซอร์ เพียงแค่เลือกจากตัวเลือกแบบเลื่อนลงแล้วคลิกที่
    ปุ่ม <strong>"ดูตัวเลือกเพิ่มเติม"</strong> คุณสามารถปรับแต่งลักษณะและพฤติกรรมของเอฟเฟกต์เพื่อให้เหมาะกับสไตล์ของคุณได้ดียิ่งขึ้น
  </p>

