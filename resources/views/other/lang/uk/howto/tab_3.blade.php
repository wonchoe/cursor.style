<div class="howto-step">
  <h2>Як застосувати курсор з вашої колекції</h2>

  <p>
    Щоб використати курсор у розширенні, спочатку потрібно <strong>додати його до своєї колекції</strong>.
    Для цього натисніть кнопку “Додати до колекції” під попереднім переглядом курсора. Обраний курсор буде додано до вашого особистого списку у розширенні.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Додати до колекції">
  </div>

  <p>
    Після того як курсор додано, відкрийте вашу колекцію у розширенні. Щоб <strong>застосувати курсор</strong>, просто натисніть на нього — і він миттєво стане активним.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Застосувати курсор">
  </div>
</div>
