<div class="howto-step">
  <h2>ከእርስዎ ስብስብ ውስጥ ጠቋሚን እንዴት መጠቀም እንደሚቻል</h2>

  <p>
    በቅጥያው ውስጥ ጠቋሚን ለመጠቀም በመጀመሪያ <strong>ወደ ስብስብዎ ማከል</strong> ያስፈልግዎታል።
    ይህን ጠቋሚውን ከቅድመ እይታ በታች ያለውን “ወደ ስብስብ አክል” የሚለውን ቁልፍ በመጫን ማድረግ ይችላሉ። ይህ የተመረጠውን ጠቋሚ በቅጥያው ውስጥ ወደ የግልዎ ዝርዝር ያክላል።
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="ወደ ስብስብ አክል">
  </div>

  <p>
    ጠቋሚው ከተጨመረ በኋላ በቅጥያው ውስጥ ያለውን ስብስብዎን ይክፈቱ። <strong>ጠቋሚውን ለመተግበር</strong> በእሱ ላይ ብቻ ጠቅ ያድርጉ - እና ወዲያውኑ የእርስዎ ንቁ ጠቋሚ ይሆናል።
  </p>

