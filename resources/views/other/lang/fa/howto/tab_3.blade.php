  <h2>نحوه اعمال یک نشانگر از مجموعه شما</h2>

  <p>
    برای استفاده از یک نشانگر در افزونه، ابتدا باید آن را به <strong>مجموعه خود اضافه کنید</strong>.
    می‌توانید این کار را با کلیک بر روی دکمه "افزودن به مجموعه" در زیر پیش‌نمایش نشانگر انجام دهید. این عمل، نشانگر انتخاب شده را به لیست شخصی شما در داخل افزونه اضافه می‌کند.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="افزودن به مجموعه">
  </div>

  <p>
    پس از افزودن نشانگر، مجموعه خود را در داخل افزونه باز کنید. برای <strong>اعمال نشانگر</strong>، فقط روی آن کلیک کنید – و فوراً نشانگر فعال شما خواهد شد.
  </p>

