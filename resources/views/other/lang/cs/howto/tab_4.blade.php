
  <h2>Jak používat kurzory v rozšíření?</h2>
  <p>
    Poté, co přidáte do svého seznamu několik kurzorů pomocí hlavního rozhraní rozšíření, možná je budete chtít spravovat.
    Odstranění kurzoru z aktivního seznamu je jednoduché: stačí najet myší na kurzor, který již nepotřebujete, a kliknout na
    <strong>ikonu koše</strong>, která se nad ním zobrazí. Tato akce okamžitě odstraní kurzor z vašeho
    viditelného seznamu, takže váš pracovní prostor bude čistý a uspořádaný.
  </p>

  <img src="/images/en/howto/delete.png" alt="Smazat kurzor" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Pokud chcete změnit efekt spojený s kurzorem, jednoduše jej vyberte z rozevíracího seznamu a klikněte na
    tlačítko <strong>"Zobrazit další možnosti"</strong>. Zde můžete doladit, jak efekt vypadá a chová se, aby lépe vyhovoval vašemu stylu.
  </p>

