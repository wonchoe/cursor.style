<div class="howto-step">
  <h2>Rychlý přístup a správa vaší sbírky</h2>

  <p>
    Chcete-li okamžitě otevřít svou sbírku, stačí <strong>kliknout na ikonu rozšíření</strong> v pravém horním rohu prohlížeče. Tím se dostanete přímo do okna vaší osobní sbírky.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Open extension popup">
  </div>

  <p>
    V záložce <strong>Sbírka</strong> můžete snadno <strong>povolit nebo zakázat rozšíření</strong> jedním kliknutím. To vám pomůže rychle se přepnout zpět na výchozí systémový kurzor, když to potřebujete.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Enable or disable extension">
  </div>

  <p>
    Můžete také <strong>zapnout vizuální efekty</strong> pro váš kurzor. Stačí přepnout efektový spínač a vaše vybrané vizuální vylepšení se okamžitě aplikuje.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Turn on effects">
  </div>
</div>
