
  <h2>كيفية استخدام المؤشرات داخل الإضافة؟</h2>
  <p>
    بعد أن تقوم بإضافة بعض المؤشرات إلى قائمتك باستخدام واجهة الإضافة الرئيسية، قد ترغب في إدارتها.
    إزالة مؤشر من قائمتك النشطة أمر بسيط: ما عليك سوى التمرير فوق المؤشر الذي لم تعد بحاجة إليه، والنقر فوق
    <strong>أيقونة سلة المهملات</strong> التي تظهر في الجزء العلوي منه. يقوم هذا الإجراء بحذف المؤشر على الفور من
    قائمتك المرئية، مما يحافظ على مساحة عملك نظيفة ومنظمة.
  </p>

  <img src="/images/ar/howto/delete.png" alt="حذف المؤشر" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    إذا كنت تريد تغيير التأثير المرتبط بمؤشر، فما عليك سوى تحديده من محدد القائمة المنسدلة والنقر فوق
    الزر <strong>"عرض خيارات إضافية"</strong>. يمكنك هناك ضبط كيفية ظهور التأثير وسلوكه بما يتناسب مع أسلوبك بشكل أفضل.
  </p>

