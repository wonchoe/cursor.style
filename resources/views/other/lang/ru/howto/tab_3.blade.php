<div class="howto-step">
  <h2>Как применить курсор из вашей коллекции</h2>

  <p>
    Чтобы использовать курсор в расширении, сначала нужно <strong>добавить его в свою коллекцию</strong>.
    Для этого нажмите кнопку “Добавить в коллекцию” под превью курсора. Выбранный курсор будет добавлен в ваш личный список внутри расширения.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Добавить в коллекцию">
  </div>

  <p>
    После добавления курсора откройте свою коллекцию в расширении. Чтобы <strong>применить курсор</strong>, просто кликните по нему — и он станет активным мгновенно.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Применить курсор">
  </div>
</div>
