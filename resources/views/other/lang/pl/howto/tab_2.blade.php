<div class="howto-step">
  <h2>Szybki dostęp do kolekcji i zarządzanie nią</h2>

  <p>
    Aby natychmiast otworzyć swoją kolekcję, wystarczy <strong>kliknąć ikonę rozszerzenia</strong> w prawym górnym rogu przeglądarki. Spowoduje to przejście bezpośrednio do okna Twojej osobistej kolekcji.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Otwórz okno rozszerzenia">
  </div>

  <p>
    W zakładce <strong>Kolekcja</strong> możesz łatwo <strong>włączyć lub wyłączyć rozszerzenie</strong> jednym kliknięciem. Pomaga to szybko powrócić do domyślnego kursora systemowego w razie potrzeby.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Włącz lub wyłącz rozszerzenie">
  </div>

  <p>
    Możesz także <strong>włączyć efekty wizualne</strong> dla swojego kursora. Po prostu przełącz przycisk efektu, a wybrane ulepszenie wizualne zostanie zastosowane natychmiast.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Włącz efekty">
  </div>
</div>
