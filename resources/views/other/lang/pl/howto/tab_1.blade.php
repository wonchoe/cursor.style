  <h2>Zainstaluj rozszerzenie i uzyskaj dostęp do swojej kolekcji</h2>

  <p>
    Aby zainstalować rozszerzenie Cursor Style, po prostu przejdź do oficjalnej strony w Chrome Web Store i kliknij przycisk <strong><a href="https://chromewebstore.google.com/detail/cursor-style-custom-curso/bmjmipppabdlpjccanalncobmbacckjn" target="_blank">"Dodaj do Chrome"</a></strong>:
  </p>

  <div class="howto-img">
    <a href="https://chromewebstore.google.com/detail/cursor-style-custom-curso/bmjmipppabdlpjccanalncobmbacckjn" target="_blank">
      <img src="/images/en/howto/installextension.png" alt="Zainstaluj rozszerzenie">
    </a>
  </div>

  <p>
    Po instalacji, w pasku nawigacyjnym strony pojawi się nowa pozycja menu o nazwie <strong>"Moja Kolekcja"</strong>. W tej sekcji możesz zarządzać wszystkimi swoimi ulubionymi kursorami i efektami w jednym, wygodnym miejscu.
  </p>

