<h2>Jak używać kursorów w rozszerzeniu?</h2>
  <p>
    Po dodaniu kilku kursorów do swojej listy za pomocą głównego interfejsu rozszerzenia, możesz chcieć nimi zarządzać.
    Usunięcie kursora z aktywnej listy jest proste: wystarczy najechać kursorem na ten, którego już nie potrzebujesz, i kliknąć
    <strong>ikonę kosza</strong>, która się nad nim pojawi. Ta czynność natychmiast usuwa kursor z Twojej
    widocznej listy, zachowując porządek i organizację w miejscu pracy.
  </p>

  <img src="/images/en/howto/delete.png" alt="Usuń kursor" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Jeśli chcesz zmienić efekt powiązany z kursorem, po prostu wybierz go z rozwijanego selektora i kliknij
    przycisk <strong>"Zobacz dodatkowe opcje"</strong>. Tam możesz dopracować wygląd i działanie efektu, aby lepiej pasował do Twojego stylu.
  </p>

