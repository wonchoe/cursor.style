<div class="howto-step">
  <h2>আপনার সংগ্রহ থেকে একটি কার্সার কীভাবে প্রয়োগ করবেন</h2>

  <p>
    এক্সটেনশনে একটি কার্সার ব্যবহার করতে, প্রথমে আপনাকে এটিকে আপনার <strong>সংগ্রহে যোগ করতে হবে</strong>।
    আপনি কার্সার প্রিভিউয়ের নিচে “সংগ্রহে যোগ করুন” বোতামে ক্লিক করে এটি করতে পারেন। এটি নির্বাচিত কার্সারটিকে এক্সটেনশনের ভিতরে আপনার ব্যক্তিগত তালিকায় যোগ করে।
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="সংগ্রহে যোগ করুন">
  </div>

  <p>
    কার্সার যোগ করার পরে, এক্সটেনশনের ভিতরে আপনার সংগ্রহটি খুলুন। <strong>কার্সার প্রয়োগ করতে</strong>, কেবল এটিতে ক্লিক করুন – এবং এটি তাৎক্ষণিকভাবে আপনার সক্রিয় কার্সার হয়ে যাবে।
  </p>

