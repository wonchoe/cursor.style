  <h2>Fikia na udhibiti mkusanyiko wako haraka</h2>

  <p>
    Ili kufungua mkusanyiko wako mara moja, bonyeza tu <strong>ikoni ya kiendelezi</strong> kwenye kona ya juu kulia ya kivinjari chako. Hii itakupeleka moja kwa moja kwenye dirisha lako la mkusanyiko wa kibinafsi.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Fungua dirisha ibukizi la kiendelezi">
  </div>

  <p>
    Ndani ya kichupo cha <strong>Mkusanyiko</strong>, unaweza <strong>kuwezesha au kulemaza kiendelezi</strong> kwa kubofya mara moja. Hii hukusaidia kubadilisha haraka kurudi kwenye kielekezi cha mfumo chaguo-msingi inapohitajika.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Washa au zima kiendelezi">
  </div>

  <p>
    Unaweza pia <strong>kuwezesha athari za kuona</strong> kwa kielekezi chako. Washa tu swichi ya athari, na uimarishaji wako wa kuona uliochaguliwa utatumika mara moja.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Washa athari">
  </div>
