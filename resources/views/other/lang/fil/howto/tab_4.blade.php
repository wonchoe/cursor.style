
  <h2>Paano gamitin ang mga cursor sa loob ng extension?</h2>
  <p>
    Pagkatapos mong madagdagan ng ilang cursor sa iyong listahan gamit ang pangunahing interface ng extension, baka gusto mong pamahalaan ang mga ito.
    Madali lang ang pag-alis ng isang cursor mula sa iyong aktibong listahan: i-hover lang ang cursor na hindi mo na kailangan, at i-click ang
    <strong>icon ng basurahan</strong> na lumilitaw sa ibabaw nito. Agad na binubura ng aksyong ito ang cursor mula sa iyong
    nakikitang listahan, na pinapanatiling malinis at maayos ang iyong workspace.
  </p>

  <img src="/images/en/howto/delete.png" alt="Burahin ang cursor" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Kung gusto mong baguhin ang epekto na nauugnay sa isang cursor, piliin lang ito mula sa dropdown selector at i-click ang
    button na <strong>"Tingnan ang mga karagdagang opsyon"</strong>. Doon mo maaaring isaayos kung paano titingnan at gagana ang epekto upang mas angkop sa iyong estilo.
  </p

