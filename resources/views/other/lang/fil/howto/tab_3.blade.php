<div class="howto-step">
  <h2>Paano ilapat ang isang cursor mula sa iyong koleksyon</h2>

  <p>
    Upang magamit ang isang cursor sa extension, kailangan mo munang <strong>idagdag ito sa iyong koleksyon</strong>.
    Magagawa mo ito sa pamamagitan ng pag-click sa pindutang “Idagdag sa Koleksyon” sa ilalim ng preview ng cursor. Idinagdag nito ang napiling cursor sa iyong personal na listahan sa loob ng extension.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Idagdag sa koleksyon">
  </div>

  <p>
    Kapag naidagdag na ang cursor, buksan ang iyong koleksyon sa loob ng extension. Upang <strong>ilapat ang cursor</strong>, i-click lamang ito – at ito ay magiging iyong aktibong cursor kaagad.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Ilapat ang cursor">
  </div>
