<div class="howto-step">
  <h2>Mabilisang i-access at pamahalaan ang iyong koleksyon</h2>

  <p>
    Upang agad na buksan ang iyong koleksyon, i-<strong>click lamang ang icon ng extension</strong> sa kanang itaas na sulok ng iyong browser. Dadalhin ka nito nang direkta sa iyong personal na bintana ng koleksyon.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Buksan ang popup ng extension">
  </div>

  <p>
    Sa loob ng tab na <strong>Koleksyon</strong>, madali mong <strong>maaring i-enable o i-disable ang extension</strong> sa isang click lamang. Nakakatulong ito upang mabilis kang makabalik sa default na cursor ng system kung kinakailangan.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="I-enable o i-disable ang extension">
  </div>

  <p>
    Maari mo ring <strong>i-enable ang mga visual effects</strong> para sa iyong cursor. I-toggle lamang ang switch ng effect, at ang iyong napiling visual enhancement ay agad na ilalapat.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="I-on ang mga effects">
  </div>
