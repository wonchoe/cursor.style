  <h2>अपने संग्रह को जल्दी से एक्सेस और प्रबंधित करें</h2>

  <p>
    अपने संग्रह को तुरंत खोलने के लिए, बस अपने ब्राउज़र के ऊपरी-दाएं कोने में <strong>एक्सटेंशन आइकन पर क्लिक करें</strong>। यह आपको सीधे आपके व्यक्तिगत संग्रह विंडो पर ले जाएगा।
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Open extension popup">
  </div>

  <p>
    <strong>संग्रह</strong> टैब के अंदर, आप आसानी से एक क्लिक से <strong>एक्सटेंशन को सक्षम या अक्षम</strong> कर सकते हैं। यह आपको आवश्यकतानुसार जल्दी से डिफ़ॉल्ट सिस्टम कर्सर पर वापस स्विच करने में मदद करता है।
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Enable or disable extension">
  </div>

  <p>
    आप अपने कर्सर के लिए <strong>विज़ुअल इफ़ेक्ट भी सक्षम</strong> कर सकते हैं। बस इफ़ेक्ट स्विच को टॉगल करें, और आपका चयनित दृश्य संवर्धन तुरंत लागू हो जाएगा।
  </p>

