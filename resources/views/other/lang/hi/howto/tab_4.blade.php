<h2>एक्सटेंशन के अंदर कर्सर का उपयोग कैसे करें?</h2>
  <p>
    मुख्य एक्सटेंशन इंटरफेस का उपयोग करके अपनी सूची में कुछ कर्सर जोड़ने के बाद, आप उन्हें प्रबंधित करना चाह सकते हैं।
    अपने सक्रिय सूची से कर्सर हटाना सरल है: बस उस कर्सर पर होवर करें जिसकी आपको अब आवश्यकता नहीं है, और उस पर दिखाई देने वाले
    <strong>कचरा बिन आइकन</strong> पर क्लिक करें। यह क्रिया तुरंत आपके
    दृश्यमान सूची से कर्सर को हटा देती है, जिससे आपका कार्यक्षेत्र साफ और व्यवस्थित रहता है।
  </p>

  <img src="/images/en/howto/delete.png" alt="कर्सर हटाएँ" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    यदि आप किसी कर्सर से जुड़े प्रभाव को बदलना चाहते हैं, तो बस उसे ड्रॉपडाउन सेलेक्टर से चुनें और
    <strong>"अतिरिक्त विकल्प देखें"</strong> बटन पर क्लिक करें। वहां आप इस प्रभाव को बेहतर ढंग से अनुकूलित कर सकते हैं कि यह कैसा दिखता है और कैसे व्यवहार करता है ताकि आपकी शैली के अनुकूल हो सके।
  </p>

