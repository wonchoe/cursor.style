<h2>Cursor Style நீட்டிப்புக்குள் cursors-ஐ எவ்வாறு பயன்படுத்துவது?</h2>
  <p>
    முக்கிய நீட்டிப்பு இடைமுகத்தைப் பயன்படுத்தி உங்கள் பட்டியலில் சில கர்சர்களைச் சேர்த்த பிறகு, அவற்றை நீங்கள் நிர்வகிக்க விரும்பலாம்.
    உங்கள் செயலில் உள்ள பட்டியலிருந்து ஒரு கர்சரை அகற்றுவது எளிது: உங்களுக்கு இனித் தேவையில்லாத கர்சரின் மீது வட்டமிட்டு, அதன் மேல் தோன்றும்
    <strong>குப்பைத் தொட்டி ஐகானை</strong> கிளிக் செய்யவும். இந்தச் செயல் உங்கள் கர்சரை உடனடியாக உங்கள்
    தெரியும் பட்டியலில் இருந்து நீக்குகிறது, உங்கள் பணியிடத்தை சுத்தமாகவும் ஒழுங்காகவும் வைத்திருக்கும்.
  </p>

