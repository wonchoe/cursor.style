  <p>
    ஒரு கர்சருடன் தொடர்புடைய விளைவை மாற்ற விரும்பினால், அதை கீழ்தோன்றும் தேர்வியிலிருந்து தேர்ந்தெடுத்து,  
    <strong>"கூடுதல் விருப்பங்களை காட்டு"</strong> பொத்தானைக் கிளிக் செய்யவும். அங்கு, உங்கள் நடைக்கு ஏற்றவாறு விளைவு எப்படி இருக்கும் மற்றும் எவ்வாறு செயல்படும் என்பதை நன்றாக மாற்றியமைக்கலாம்.
  </p>

