<div class="howto-step">
  <h2>Γρήγορη πρόσβαση και διαχείριση της συλλογής σας</h2>

  <p>
    Για να ανοίξετε άμεσα τη συλλογή σας, απλώς <strong>κάντε κλικ στο εικονίδιο της επέκτασης</strong> στην επάνω δεξιά γωνία του προγράμματος περιήγησής σας. Αυτό θα σας μεταφέρει απευθείας στο παράθυρο της προσωπικής σας συλλογής.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Άνοιγμα αναδυόμενου παραθύρου επέκτασης">
  </div>

  <p>
    Μέσα στην καρτέλα <strong>Συλλογή</strong>, μπορείτε εύκολα να <strong>ενεργοποιήσετε ή να απενεργοποιήσετε την επέκταση</strong> με ένα κλικ. Αυτό σας βοηθά να επιστρέψετε γρήγορα στον προεπιλεγμένο κέρσορα του συστήματος όταν χρειάζεται.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Ενεργοποίηση ή απενεργοποίηση επέκτασης">
  </div>

  <p>
    Μπορείτε επίσης να <strong>ενεργοποιήσετε οπτικά εφέ</strong> για τον κέρσορά σας. Απλώς αλλάξτε τον διακόπτη εφέ και η επιλεγμένη οπτική βελτίωση θα εφαρμοστεί αμέσως.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Ενεργοποίηση εφέ">
  </div>
</div>
