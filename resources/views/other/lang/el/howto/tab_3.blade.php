  <h2>Πώς να εφαρμόσετε έναν κέρσορα από τη συλλογή σας</h2>

  <p>
    Για να χρησιμοποιήσετε έναν κέρσορα στην επέκταση, πρέπει πρώτα να τον <strong>προσθέσετε στη συλλογή σας</strong>.
    Μπορείτε να το κάνετε αυτό κάνοντας κλικ στο κουμπί "Προσθήκη στη συλλογή" κάτω από την προεπισκόπηση του κέρσορα. Αυτό προσθέτει τον επιλεγμένο κέρσορα στην προσωπική σας λίστα μέσα στην επέκταση.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Προσθήκη στη συλλογή">
  </div>

  <p>
    Μόλις προστεθεί ο κέρσορας, ανοίξτε τη συλλογή σας μέσα στην επέκταση. Για να <strong>εφαρμόσετε τον κέρσορα</strong>, απλώς κάντε κλικ πάνω του – και θα γίνει ο ενεργός σας κέρσορας αμέσως.
  </p>

