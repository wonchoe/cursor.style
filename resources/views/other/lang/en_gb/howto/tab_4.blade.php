
  <h2>How to use cursors inside extension?</h2>
  <p>
    After you've added a few cursors to your list using the main extension interface, you might want to manage them.
    Removing a cursor from your active list is simple: just hover over the cursor you no longer need, and click the
    <strong>trash bin icon</strong> that appears on top of it. This action immediately deletes the cursor from your
    visible list, keeping your workspace clean and organised.
  </p>

  <img src="/images/en/howto/delete.png" alt="Delete cursor" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    If you want to change the effect associated with a cursor, simply select it from the dropdown selector and click
    the <strong>"View additional options"</strong> button. There you can fine-tune how the effect looks and behaves to better suit your style.
  </p

