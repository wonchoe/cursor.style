<div class="howto-step">
  <h2>How to apply a cursor from your collection</h2>

  <p>
    To use a cursor in the extension, you first need to <strong>add it to your collection</strong>.
    You can do this by clicking the “Add to Collection” button under the cursor preview. This adds the selected cursor to your personal list inside the extension.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Add to collection">
  </div>

  <p>
    Once the cursor has been added, open your collection inside the extension. To <strong>apply the cursor</strong>, just click on it – and it will become your active cursor instantly.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Apply cursor">
  </div>
</div>
