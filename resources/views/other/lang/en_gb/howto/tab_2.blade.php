<div class="howto-step">
  <h2>Access and manage your collection quickly</h2>

  <p>
    To instantly open your collection, just <strong>click on the extension icon</strong> in the top-right corner of your browser. This will take you directly to your personal collection window.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Open extension popup">
  </div>

  <p>
    Inside the <strong>Collection</strong> tab, you can easily <strong>enable or disable the extension</strong> with one click. This helps you quickly switch back to the default system cursor when needed.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Enable or disable extension">
  </div>

  <p>
    You can also <strong>enable visual effects</strong> for your cursor. Just toggle the effect switch, and your selected visual enhancement will be applied instantly.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Turn on effects">
  </div>
