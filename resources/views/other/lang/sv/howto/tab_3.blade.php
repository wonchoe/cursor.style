<div class="howto-step">
  <h2>Hur man använder en markör från din samling</h2>

  <p>
    För att använda en markör i tillägget måste du först <strong>lägga till den i din samling</strong>.
    Du kan göra detta genom att klicka på knappen "Lägg till i samling" under markörförhandsgranskningen. Detta lägger till den valda markören i din personliga lista i tillägget.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Lägg till i samling">
  </div>

  <p>
    När markören har lagts till, öppna din samling i tillägget. För att <strong>använda markören</strong>, klicka bara på den – så blir den din aktiva markör direkt.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Använd markör">
  </div>
</div>
