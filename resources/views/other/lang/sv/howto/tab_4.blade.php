
  <h2>Hur man använder pekare inuti tillägget?</h2>
  <p>
    När du har lagt till några pekare i din lista med hjälp av huvudgränssnittet i tillägget kanske du vill hantera dem.
    Att ta bort en pekare från din aktiva lista är enkelt: sväva bara över den pekare du inte längre behöver och klicka på
    <strong>papperskorgsikonen</strong> som visas ovanpå den. Denna åtgärd raderar omedelbart pekaren från din
    synliga lista och håller din arbetsyta ren och organiserad.
  </p>

  <img src="/images/en/howto/delete.png" alt="Ta bort pekare" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Om du vill ändra effekten som är associerad med en pekare väljer du den helt enkelt från rullgardinsvalet och klickar på
    knappen <strong>"Visa ytterligare alternativ"</strong>. Där kan du finjustera hur effekten ser ut och beter sig för att bättre passa din stil.
  </p>

