<div class="howto-step">
  <h2>Kuidas rakendada kursorit oma kollektsioonist</h2>

  <p>
    Kursori kasutamiseks laienduses peate kõigepealt <strong>lisama selle oma kollektsiooni</strong>.
    Seda saate teha, klõpsates kursori eelvaate all nuppu "Lisa kollektsiooni". See lisab valitud kursori teie isiklikku nimekirja laienduse sees.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Lisa kollektsiooni">
  </div>

  <p>
    Kui kursor on lisatud, avage oma kollektsioon laienduse sees. <strong>Kursori rakendamiseks</strong> klõpsake lihtsalt sellel – ja sellest saab kohe teie aktiivne kursor.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Rakenda kursor">
  </div>
