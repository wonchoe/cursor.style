  <h2>Juurdepääs kollektsioonile ja selle haldamine kiiresti</h2>

  <p>
    Kollektsiooni koheseks avamiseks <strong>klõpsake laienduse ikooni</strong> brauseri paremas ülanurgas. See viib teid otse teie isikliku kollektsiooni aknasse.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Ava laienduse hüpikaken">
  </div>

  <p>
    Vahekaardil <strong>Kollektsioon</strong> saate laienduse hõlpsasti <strong>lubada või keelata</strong> ühe klõpsuga. See aitab teil kiiresti naasta vaikekursorile, kui vaja.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Luba või keela laiendus">
  </div>

  <p>
    Samuti saate kursori jaoks <strong>visuaalseid efekte lubada</strong>. Lihtsalt lülitage efekti lüliti sisse ja teie valitud visuaalne täiustus rakendatakse koheselt.
  </p>

