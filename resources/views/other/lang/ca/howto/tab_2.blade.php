<div class="howto-step">
  <h2>Accedeix i gestiona la teva col·lecció ràpidament</h2>

  <p>
    Per obrir la teva col·lecció a l'instant, només has de <strong>fer clic a la icona de l'extensió</strong> a la cantonada superior dreta del teu navegador. Això et portarà directament a la finestra de la teva col·lecció personal.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Obre la finestra emergent de l'extensió">
  </div>

  <p>
    Dins de la pestanya <strong>Col·lecció</strong>, pots <strong>activar o desactivar fàcilment l'extensió</strong> amb un sol clic. Això t'ajuda a tornar ràpidament al cursor del sistema per defecte quan sigui necessari.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Activa o desactiva l'extensió">
  </div>

  <p>
    També pots <strong>activar efectes visuals</strong> per al teu cursor. Només has d'activar l'interruptor d'efecte, i la millora visual seleccionada s'aplicarà a l'instant.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Activa els efectes">
  </div>
</div>
