  <h2>Com aplicar un cursor de la teva col·lecció</h2>

  <p>
    Per utilitzar un cursor a l'extensió, primer has de <strong>afegir-lo a la teva col·lecció</strong>.
    Pots fer-ho fent clic al botó “Afegeix a la col·lecció” sota la previsualització del cursor. Això afegeix el cursor seleccionat a la teva llista personal dins de l'extensió.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Afegeix a la col·lecció">
  </div>

  <p>
    Un cop afegit el cursor, obre la teva col·lecció dins de l'extensió. Per <strong>aplicar el cursor</strong>, només has de fer-hi clic, i es convertirà instantàniament en el teu cursor actiu.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Aplica el cursor">
  </div>
