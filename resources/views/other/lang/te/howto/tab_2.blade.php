  <h2>మీ సేకరణను త్వరగా యాక్సెస్ చేయండి మరియు నిర్వహించండి</h2>

  <p>
    మీ సేకరణను తక్షణమే తెరవడానికి, మీ బ్రౌజర్ యొక్క కుడి-ఎగువ మూలలోని <strong>ఎక్స్‌టెన్షన్ చిహ్నంపై క్లిక్ చేయండి</strong>. ఇది మిమ్మల్ని నేరుగా మీ వ్యక్తిగత సేకరణ విండోకి తీసుకెళుతుంది.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="ఎక్స్‌టెన్షన్ పాప్‌అప్‌ని తెరవండి">
  </div>

  <p>
    <strong>సేకరణ</strong> ట్యాబ్‌లో, మీరు ఒకే క్లిక్‌తో <strong>ఎక్స్‌టెన్షన్‌ను ప్రారంభించవచ్చు లేదా నిలిపివేయవచ్చు</strong>. అవసరమైనప్పుడు డిఫాల్ట్ సిస్టమ్ కర్సర్‌కి త్వరగా మారడానికి ఇది మీకు సహాయపడుతుంది.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="ఎక్స్‌టెన్షన్‌ను ప్రారంభించండి లేదా నిలిపివేయండి">
  </div>

  <p>
    మీరు మీ కర్సర్‌ కోసం <strong>విజువల్ ఎఫెక్ట్‌లను కూడా ప్రారంభించవచ్చు</strong>. ఎఫెక్ట్ స్విచ్‌ని టోగుల్ చేయండి, మరియు మీ ఎంచుకున్న విజువల్ ఎన్‌హాన్స్‌మెంట్ తక్షణమే వర్తించబడుతుంది.
  </p>

