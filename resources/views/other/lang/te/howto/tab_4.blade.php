
  <h2>కర్సర్‌లను ఎక్స్‌టెన్షన్ లోపల ఎలా ఉపయోగించాలి?</h2>
  <p>
    మీరు ప్రధాన ఎక్స్‌టెన్షన్ ఇంటర్‌ఫేస్‌ని ఉపయోగించి మీ జాబితాకు కొన్ని కర్సర్‌లను జోడించిన తర్వాత, మీరు వాటిని నిర్వహించాలనుకోవచ్చు.
    మీరు ఇకపై అవసరం లేని కర్సర్‌ను తీసివేయడం సులభం: మీకు ఇకపై అవసరం లేని కర్సర్ పైకి వెళ్లి, దానిపై కనిపించే 
    <strong>చెత్త బుట్ట చిహ్నాన్ని</strong> క్లిక్ చేయండి. ఈ చర్య వెంటనే మీ
    దృశ్య జాబితా నుండి కర్సర్‌ను తొలగిస్తుంది, మీ పని స్థలాన్ని శుభ్రంగా మరియు వ్యవస్థీకృతంగా ఉంచుతుంది.
  </p>

  <img src="/images/en/howto/delete.png" alt="కర్సర్‌ను తొలగించండి" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    మీరు కర్సర్‌తో అనుబంధించబడిన ప్రభావాన్ని మార్చాలనుకుంటే, డ్రాప్‌డౌన్ సెలెక్టర్ నుండి దాన్ని ఎంచుకుని, 
    <strong>"అదనపు ఎంపికలను వీక్షించండి"</strong> బటన్‌ను క్లిక్ చేయండి. మీ శైలికి బాగా సరిపోయేలా ప్రభావం ఎలా ఉంటుందో మరియు ఎలా ప్రవర్తిస్తుందో అక్కడ మీరు చక్కగా ట్యూన్ చేయవచ్చు.
  </p

