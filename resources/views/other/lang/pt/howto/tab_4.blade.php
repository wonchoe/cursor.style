<h2>Como usar os cursores na extensão?</h2>
  <p>
    Depois de adicionar alguns cursores à sua lista usando a interface principal da extensão, talvez queira geri-los.
    Remover um cursor da sua lista ativa é simples: basta passar o rato sobre o cursor que não precisa mais e clicar no
    <strong>ícone do caixote do lixo</strong> que aparece em cima dele. Esta ação elimina imediatamente o cursor da sua
    lista visível, mantendo o seu espaço de trabalho limpo e organizado.
  </p>

  <img src="/images/en/howto/delete.png" alt="Apagar cursor" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Se deseja alterar o efeito associado a um cursor, basta selecioná-lo no seletor pendente e clicar no botão
    <strong>"Ver opções adicionais"</strong>. Lá pode ajustar a aparência e o comportamento do efeito para melhor se adequar ao seu estilo.
  </p>

