<div class="howto-step">
  <h2>Aceda e gestione a sua coleção rapidamente</h2>

  <p>
    Para abrir instantaneamente a sua coleção, basta <strong>clicar no ícone da extensão</strong> no canto superior direito do seu navegador. Isso irá levá-lo diretamente para a janela da sua coleção pessoal.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Abrir popup da extensão">
  </div>

  <p>
    Dentro do separador <strong>Coleção</strong>, pode facilmente <strong>ativar ou desativar a extensão</strong> com um clique. Isto ajuda a voltar rapidamente para o cursor do sistema predefinido quando necessário.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Ativar ou desativar extensão">
  </div>

  <p>
    Também pode <strong>ativar efeitos visuais</strong> para o seu cursor. Basta alternar o interruptor de efeito, e a sua melhoria visual selecionada será aplicada instantaneamente.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Ativar efeitos">
  </div>
</div>
