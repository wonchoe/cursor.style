      <img src="/images/en/howto/installextension.png" alt="Instalar extensão">
    </a>
  </div>

  <p>
    Após a instalação, um novo item de menu chamado <strong>"Minha Coleção"</strong> aparecerá na barra de navegação do site. Esta secção permite-lhe gerir todos os seus cursores e efeitos favoritos num só lugar conveniente.
