  <h2>拡張機能をインストールして、コレクションにアクセスする</h2>

  <p>
    Cursor Style拡張機能をインストールするには、Chromeウェブストアの公式ページにアクセスし、<strong><a href="https://chromewebstore.google.com/detail/cursor-style-custom-curso/bmjmipppabdlpjccanalncobmbacckjn" target="_blank">"Chromeに追加"</a></strong>ボタンをクリックしてください：
  </p>

  <div class="howto-img">
    <a href="https://chromewebstore.google.com/detail/cursor-style-custom-curso/bmjmipppabdlpjccanalncobmbacckjn" target="_blank">
      <img src="/images/en/howto/installextension.png" alt="拡張機能をインストール">
    </a>
  </div>

  <p>
    インストール後、<strong>"My Collection"</strong>という新しいメニュー項目がウェブサイトのナビゲーションバーに表示されます。このセクションでは、お気に入りのすべてのカーソルとエフェクトを1か所で管理できます。
  </p>

