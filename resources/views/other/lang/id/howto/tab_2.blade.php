<div class="howto-step">
  <h2>Akses dan kelola koleksi Anda dengan cepat</h2>

  <p>
    Untuk membuka koleksi Anda secara instan, cukup <strong>klik ikon ekstensi</strong> di sudut kanan atas browser Anda. Ini akan membawa Anda langsung ke jendela koleksi pribadi Anda.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Buka popup ekstensi">
  </div>

  <p>
    Di dalam tab <strong>Koleksi</strong>, Anda dapat dengan mudah <strong>mengaktifkan atau menonaktifkan ekstensi</strong> dengan satu klik. Ini membantu Anda dengan cepat beralih kembali ke kursor sistem default bila diperlukan.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Aktifkan atau nonaktifkan ekstensi">
  </div>

  <p>
    Anda juga dapat <strong>mengaktifkan efek visual</strong> untuk kursor Anda. Cukup alihkan sakelar efek, dan peningkatan visual yang Anda pilih akan diterapkan secara instan.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Aktifkan efek">
  </div>
</div>
