
  <h2>Làm thế nào để sử dụng con trỏ trong tiện ích mở rộng?</h2>
  <p>
    Sau khi bạn đã thêm một vài con trỏ vào danh sách của mình bằng giao diện tiện ích mở rộng chính, bạn có thể muốn quản lý chúng.
    Xóa một con trỏ khỏi danh sách đang hoạt động của bạn rất đơn giản: chỉ cần di chuột qua con trỏ bạn không cần nữa và nhấp vào
    <strong>biểu tượng thùng rác</strong> xuất hiện trên đầu nó. Hành động này ngay lập tức xóa con trỏ khỏi
    danh sách hiển thị của bạn, giữ cho không gian làm việc của bạn sạch sẽ và ngăn nắp.
  </p>

  <img src="/images/en/howto/delete.png" alt="Xóa con trỏ" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Nếu bạn muốn thay đổi hiệu ứng liên quan đến một con trỏ, chỉ cần chọn nó từ trình chọn thả xuống và nhấp vào
    nút <strong>"Xem các tùy chọn bổ sung"</strong>. Tại đó, bạn có thể tinh chỉnh giao diện và hành vi của hiệu ứng để phù hợp hơn với phong cách của bạn.
  </p>

