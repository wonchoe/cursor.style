<div class="howto-step">
  <h2>Truy cập và quản lý bộ sưu tập của bạn nhanh chóng</h2>

  <p>
    Để mở bộ sưu tập của bạn ngay lập tức, chỉ cần <strong>nhấp vào biểu tượng tiện ích</strong> ở góc trên bên phải của trình duyệt. Thao tác này sẽ đưa bạn trực tiếp đến cửa sổ bộ sưu tập cá nhân của bạn.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Mở cửa sổ bật lên tiện ích">
  </div>

  <p>
    Bên trong tab <strong>Bộ sưu tập</strong>, bạn có thể dễ dàng <strong>bật hoặc tắt tiện ích</strong> bằng một cú nhấp chuột. Điều này giúp bạn nhanh chóng chuyển về con trỏ hệ thống mặc định khi cần.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Bật hoặc tắt tiện ích">
  </div>

  <p>
    Bạn cũng có thể <strong>bật hiệu ứng hình ảnh</strong> cho con trỏ của mình. Chỉ cần chuyển đổi công tắc hiệu ứng và cải tiến hình ảnh đã chọn của bạn sẽ được áp dụng ngay lập tức.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Bật hiệu ứng">
  </div>
</div>
