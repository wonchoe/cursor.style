  <h2>Ako použiť kurzor z vašej zbierky</h2>

  <p>
    Ak chcete použiť kurzor v rozšírení, musíte ho najprv <strong>pridať do svojej zbierky</strong>.
    Urobíte to kliknutím na tlačidlo “Pridať do zbierky” pod náhľadom kurzora. Týmto sa vybraný kurzor pridá do vášho osobného zoznamu v rámci rozšírenia.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Pridať do zbierky">
  </div>

  <p>
    Po pridaní kurzora otvorte svoju zbierku v rozšírení. Ak chcete <strong>použiť kurzor</strong>, stačí naň kliknúť – a okamžite sa stane vaším aktívnym kurzorom.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Použiť kurzor">
  </div>
</div>
