<div class="howto-step">
  <h2>So wenden Sie einen Cursor aus Ihrer Sammlung an</h2>

  <p>
    Um einen Cursor in der Erweiterung zu verwenden, müssen Sie ihn zuerst <strong>Ihrer Sammlung hinzufügen</strong>.
    Dies können Sie tun, indem Sie unter der Cursorvorschau auf die Schaltfläche „Zur Sammlung hinzufügen“ klicken. Dadurch wird der ausgewählte Cursor zu Ihrer persönlichen Liste innerhalb der Erweiterung hinzugefügt.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Zur Sammlung hinzufügen">
  </div>

  <p>
    Sobald der Cursor hinzugefügt wurde, öffnen Sie Ihre Sammlung innerhalb der Erweiterung. Um den Cursor <strong>anzuwenden</strong>, klicken Sie einfach darauf – und er wird sofort zu Ihrem aktiven Cursor.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Cursor anwenden">
  </div>
</div>
