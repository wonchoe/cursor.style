<div class="howto-step">
  <h2>Schneller Zugriff und Verwaltung Ihrer Sammlung</h2>

  <p>
    Um Ihre Sammlung sofort zu öffnen, <strong>klicken Sie einfach auf das Erweiterungssymbol</strong> in der oberen rechten Ecke Ihres Browsers. Dadurch gelangen Sie direkt zu Ihrem persönlichen Sammlungsfenster.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Erweiterungs-Popup öffnen">
  </div>

  <p>
    In der <strong>Sammlungs</strong>-Registerkarte können Sie die <strong>Erweiterung mit einem Klick aktivieren oder deaktivieren</strong>. Dies hilft Ihnen, bei Bedarf schnell zum Standard-Systemcursor zurückzukehren.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Erweiterung aktivieren oder deaktivieren">
  </div>

  <p>
    Sie können auch <strong>visuelle Effekte</strong> für Ihren Cursor aktivieren.  Schalten Sie einfach den Effektschalter um, und Ihre ausgewählte visuelle Verbesserung wird sofort angewendet.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Effekte aktivieren">
  </div>
</div>
