<h2>Wie man Cursor innerhalb der Erweiterung verwendet?</h2>
  <p>
    Nachdem Sie Ihrer Liste einige Cursor mithilfe der Hauptoberfläche der Erweiterung hinzugefügt haben, möchten Sie diese möglicherweise verwalten.
    Das Entfernen eines Cursors aus Ihrer aktiven Liste ist einfach: Bewegen Sie einfach den Mauszeiger über den Cursor, den Sie nicht mehr benötigen, und klicken Sie auf das
    <strong>Mülleimer-Symbol</strong>, das darüber erscheint. Dadurch wird der Cursor sofort aus Ihrer
    sichtbaren Liste gelöscht, wodurch Ihr Arbeitsbereich sauber und organisiert bleibt.
  </p>

  <img src="/images/de/howto/delete.png" alt="Cursor löschen" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Wenn Sie den einem Cursor zugeordneten Effekt ändern möchten, wählen Sie ihn einfach aus der Dropdown-Auswahl aus und klicken Sie
    auf die Schaltfläche <strong>"Zusätzliche Optionen anzeigen"</strong>. Dort können Sie feinjustieren, wie der Effekt aussieht und sich verhält, um Ihrem Stil besser zu entsprechen.
  </p>

