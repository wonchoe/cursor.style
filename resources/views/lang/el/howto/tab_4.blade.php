
  <h2>Πώς να χρησιμοποιήσετε τους κέρσορες μέσα στην επέκταση;</h2>
  <p>
    Αφού έχετε προσθέσει μερικούς κέρσορες στη λίστα σας χρησιμοποιώντας την κύρια διεπαφή επέκτασης, ίσως θελήσετε να τους διαχειριστείτε.
    Η αφαίρεση ενός κέρσορα από την ενεργή σας λίστα είναι απλή: απλώς τοποθετήστε τον δείκτη του ποντικιού πάνω από τον κέρσορα που δεν χρειάζεστε πλέον και κάντε κλικ στο
    <strong>εικονίδιο του κάδου απορριμμάτων</strong> που εμφανίζεται πάνω του. Αυτή η ενέργεια διαγράφει αμέσως τον κέρσορα από την
    ορατή σας λίστα, διατηρώντας τον χώρο εργασίας σας καθαρό και οργανωμένο.
  </p>

  <img src="/images/en/howto/delete.png" alt="Διαγραφή κέρσορα" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Εάν θέλετε να αλλάξετε το εφέ που σχετίζεται με έναν κέρσορα, απλά επιλέξτε το από τον επιλογέα αναπτυσσόμενου μενού και κάντε κλικ στο
    κουμπί <strong>"Δείτε επιπλέον επιλογές"</strong>. Εκεί μπορείτε να ρυθμίσετε με ακρίβεια την εμφάνιση και τη συμπεριφορά του εφέ ώστε να ταιριάζει καλύτερα στο στυλ σας.
  </p>

