<h2>Ako používať kurzory v rozsahu rozšírenia?</h2>
  <p>
    Po pridaní niekoľkých kurzorov do zoznamu pomocou hlavného rozhrania rozšírenia ich možno budete chcieť spravovať.
    Odstránenie kurzora z aktívneho zoznamu je jednoduché: stačí prejsť myšou nad kurzor, ktorý už nepotrebujete, a kliknúť na
    <strong>ikonu koša</strong>, ktorá sa nad ním zobrazí. Táto akcia okamžite vymaže kurzor z vášho
    viditeľného zoznamu, čím zostane váš pracovný priestor čistý a usporiadaný.
  </p>

  <img src="/images/en/howto/delete.png" alt="Vymazať kurzor" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Ak chcete zmeniť efekt priradený ku kurzoru, jednoducho ho vyberte z rozbaľovacieho zoznamu a kliknite na
    tlačidlo <strong>"Zobraziť ďalšie možnosti"</strong>. Tam si môžete jemne vyladiť vzhľad a správanie efektu, aby lepšie vyhovoval vášmu štýlu.
  </p>

