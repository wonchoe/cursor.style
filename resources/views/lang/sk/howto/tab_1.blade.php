    Po inštalácii sa na navigačnej lište webovej stránky zobrazí nová položka menu s názvom <strong>"Moja zbierka"</strong>. Táto sekcia vám umožňuje spravovať všetky vaše obľúbené kurzory a efekty na jednom pohodlnom mieste.
