  <h2>Rýchly prístup a správa vašej zbierky</h2>

  <p>
    Ak chcete okamžite otvoriť svoju zbierku, stačí <strong>kliknúť na ikonu rozšírenia</strong> v pravom hornom rohu prehliadača. Tým sa dostanete priamo do okna vašej osobnej zbierky.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Open extension popup">
  </div>

  <p>
    V záložke <strong>Zbierka</strong> môžete jednoducho <strong>povoliť alebo zakázať rozšírenie</strong> jedným kliknutím. To vám pomôže rýchlo sa prepnúť späť na predvolený systémový kurzor, keď je to potrebné.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Enable or disable extension">
  </div>

  <p>
    Môžete tiež <strong>povoliť vizuálne efekty</strong> pre váš kurzor. Stačí prepnúť prepínač efektu a vaše vybrané vizuálne vylepšenie sa okamžite použije.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Turn on effects">
