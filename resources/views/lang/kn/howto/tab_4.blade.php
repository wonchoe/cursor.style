<h2>ಕರ್ಸರ್ಗಳನ್ನು ವಿಸ್ತರಣೆಯಲ್ಲಿ ಹೇಗೆ ಬಳಸುವುದು?</h2>
  <p>
    ನೀವು ಮುಖ್ಯ ವಿಸ್ತರಣೆ ಇಂಟರ್ಫೇಸ್ ಬಳಸಿ, ನಿಮ್ಮ ಪಟ್ಟಿಗೆ ಕೆಲವು ಕರ್ಸರ್ಗಳನ್ನು ಸೇರಿಸಿದ ನಂತರ, ನೀವು ಅವುಗಳನ್ನು ನಿರ್ವಹಿಸಲು ಬಯಸಬಹುದು.
    ನಿಮ್ಮ ಸಕ್ರಿಯ ಪಟ್ಟಿಯಿಂದ ಕರ್ಸರ್ ಅನ್ನು ತೆಗೆದುಹಾಕುವುದು ಸರಳವಾಗಿದೆ: ನಿಮಗೆ ಇನ್ನು ಮುಂದೆ ಅಗತ್ಯವಿಲ್ಲದ ಕರ್ಸರ್ ಮೇಲೆ ಸುಳಿದಾಡಿ ಮತ್ತು ಅದರ ಮೇಲೆ ಕಾಣಿಸಿಕೊಳ್ಳುವ <strong>ಕಸದ ಬುಟ್ಟಿ ಐಕಾನ್</strong> ಅನ್ನು ಕ್ಲಿಕ್ ಮಾಡಿ.
    ಈ ಕ್ರಿಯೆಯು ತಕ್ಷಣವೇ ನಿಮ್ಮ ಗೋಚರ ಪಟ್ಟಿಯಿಂದ ಕರ್ಸರ್ ಅನ್ನು ಅಳಿಸುತ್ತದೆ, ನಿಮ್ಮ ಕೆಲಸದ ಸ್ಥಳವನ್ನು ಸ್ವಚ್ಛವಾಗಿ ಮತ್ತು ವ್ಯವಸ್ಥಿತವಾಗಿರಿಸುತ್ತದೆ.
  </p>

