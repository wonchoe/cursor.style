<div class="howto-step">
  <h2>컬렉션을 빠르게 액세스하고 관리하세요</h2>

  <p>
    컬렉션을 즉시 열려면 브라우저 오른쪽 상단 모서리에 있는 <strong>확장 프로그램 아이콘을 클릭</strong>하세요. 그러면 개인 컬렉션 창으로 바로 이동합니다.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Open extension popup">
  </div>

  <p>
    <strong>컬렉션</strong> 탭 내에서 한 번의 클릭으로 <strong>확장 프로그램을 쉽게 활성화 또는 비활성화</strong>할 수 있습니다. 이렇게 하면 필요할 때 기본 시스템 커서로 빠르게 전환할 수 있습니다.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Enable or disable extension">
  </div>

  <p>
    또한 커서에 대한 <strong>시각 효과를 활성화</strong>할 수 있습니다. 효과 스위치를 토글하면 선택한 시각적 향상이 즉시 적용됩니다.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Turn on effects">
  </div>
