
  <h2>확장 프로그램 내에서 커서를 사용하는 방법은 무엇입니까?</h2>
  <p>
    주 확장 인터페이스를 사용하여 커서를 목록에 몇 개 추가한 후 관리하고 싶을 수 있습니다.
    활성 목록에서 커서를 제거하는 것은 간단합니다. 더 이상 필요하지 않은 커서 위에 마우스를 올리고
    해당 커서 위에 나타나는 <strong>휴지통 아이콘</strong>을 클릭하기만 하면 됩니다. 이 작업은 커서를 표시되는 목록에서 즉시 삭제하여
    작업 공간을 깨끗하고 정리된 상태로 유지합니다.
  </p>

  <img src="/images/en/howto/delete.png" alt="커서 삭제" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    커서와 연관된 효과를 변경하려면 드롭다운 선택기에서 해당 커서를 선택하고
    <strong>"추가 옵션 보기"</strong> 버튼을 클릭하기만 하면 됩니다. 여기에서 효과가 사용자 스타일에 더 잘 맞도록 효과의 모습과 동작을 미세 조정할 수 있습니다.
  </p>

