<div class="howto-step">
  <h2>컬렉션에서 커서를 적용하는 방법</h2>

  <p>
    확장 기능에서 커서를 사용하려면 먼저 <strong>컬렉션에 추가</strong>해야 합니다.
    커서 미리보기 아래의 “컬렉션에 추가” 버튼을 클릭하여 이 작업을 수행할 수 있습니다. 이렇게 하면 선택한 커서가 확장 기능 내부의 개인 목록에 추가됩니다.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="컬렉션에 추가">
  </div>

  <p>
    커서가 추가되면 확장 기능 내에서 컬렉션을 엽니다. <strong>커서를 적용</strong>하려면 커서를 클릭하기만 하면 됩니다. 그러면 즉시 활성 커서가 됩니다.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="커서 적용">
  </div>
</div>
