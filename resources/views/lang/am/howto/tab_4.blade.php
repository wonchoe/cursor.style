  <h2>ተቆጣጣሪዎችን በማራዘሚያው ውስጥ እንዴት መጠቀም እንደሚቻል?</h2>
  <p>
    ጥቂት ተቆጣጣሪዎችን በመነሻው የማራዘሚያ በይነገጽ በመጠቀም ወደ ዝርዝርዎ ካከሉ በኋላ፣ እነሱን ማስተዳደር ሊፈልጉ ይችላሉ።
    አንድ ተቆጣጣሪን ከንቁ ዝርዝርዎ ማስወገድ ቀላል ነው፡ ከአሁን በኋላ የማያስፈልግዎትን ተቆጣጣሪ ላይ አንዣብበው እና ጠቅ ያድርጉ
    በላዩ ላይ የሚታየው <strong>የቆሻሻ መጣያ አዶ</strong>። ይህ እርምጃ ተቆጣጣሪውን ከእርስዎ ወዲያውኑ ይሰርዛል
    የሚታየው ዝርዝር፣ የስራ ቦታዎን ንፁህ እና የተደራጀ ያደርጋል።
  </p>

