<div class="howto-step">
  <h2>Come applicare un cursore dalla tua collezione</h2>

  <p>
    Per usare un cursore nell'estensione, devi prima <strong>aggiungerlo alla tua collezione</strong>.
    Puoi farlo cliccando il pulsante "Aggiungi alla collezione" sotto l'anteprima del cursore. Questo aggiunge il cursore selezionato al tuo elenco personale all'interno dell'estensione.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Aggiungi alla collezione">
  </div>

  <p>
    Una volta aggiunto il cursore, apri la tua collezione all'interno dell'estensione. Per <strong>applicare il cursore</strong>, basta cliccarci sopra – e diventerà immediatamente il tuo cursore attivo.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Applica cursore">
  </div>
</div>
