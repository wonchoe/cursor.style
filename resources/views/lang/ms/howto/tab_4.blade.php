
  <h2>Bagaimana untuk menggunakan kursor di dalam sambungan?</h2>
  <p>
    Selepas anda telah menambah beberapa kursor ke senarai anda menggunakan antara muka sambungan utama, anda mungkin mahu menguruskannya.
    Membuang kursor daripada senarai aktif anda adalah mudah: cuma halakan tetikus ke atas kursor yang anda tidak perlukan lagi, dan klik
    <strong>ikon tong sampah</strong> yang muncul di atasnya. Tindakan ini serta-merta memadamkan kursor daripada
    senarai yang kelihatan, memastikan ruang kerja anda bersih dan teratur.
  </p>

  <img src="/images/en/howto/delete.png" alt="Padamkan kursor" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Jika anda mahu menukar kesan yang dikaitkan dengan kursor, cuma pilihnya daripada pemilih juntai bawah dan klik
    butang <strong>"Lihat pilihan tambahan"</strong>. Di sana anda boleh melaraskan cara kesan itu kelihatan dan berkelakuan untuk lebih sesuai dengan gaya anda.
  </p>

