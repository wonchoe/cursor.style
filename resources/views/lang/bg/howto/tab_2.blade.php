<div class="howto-step">
  <h2>Бърз достъп и управление на вашата колекция</h2>

  <p>
    За да отворите мигновено вашата колекция, просто <strong>кликнете върху иконата на разширението</strong> в горния десен ъгъл на браузъра си. Това ще ви отведе директно до прозореца с вашата лична колекция.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Open extension popup">
  </div>

  <p>
    В раздела <strong>Collection</strong> можете лесно да <strong>активирате или деактивирате разширението</strong> с едно кликване. Това ви помага бързо да се върнете към системния курсор по подразбиране, когато е необходимо.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Enable or disable extension">
  </div>

  <p>
    Също така можете да <strong>активирате визуални ефекти</strong> за вашия курсор. Просто превключете превключвателя за ефекти и избраното от вас визуално подобрение ще бъде приложено незабавно.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Turn on effects">
  </div>
</div>
