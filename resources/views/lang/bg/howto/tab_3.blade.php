<div class="howto-step">
  <h2>Как да приложите курсор от вашата колекция</h2>

  <p>
    За да използвате курсор в разширението, първо трябва да го <strong>добавите към вашата колекция</strong>.
    Можете да направите това, като кликнете върху бутона „Добави в колекцията“ под визуализацията на курсора. Това добавя избрания курсор към личния ви списък в разширението.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Добави в колекцията">
  </div>

  <p>
    След като курсорът бъде добавен, отворете вашата колекция в разширението. За да <strong>приложите курсора</strong>, просто кликнете върху него - и той ще стане вашият активен курсор незабавно.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Приложи курсора">
  </div>
</div>
