<div class="howto-step">
  <h2>Cara menerapkan kursor dari koleksi Anda</h2>

  <p>
    Untuk menggunakan kursor di ekstensi, Anda harus <strong>menambahkannya ke koleksi Anda</strong>.
    Anda dapat melakukan ini dengan mengklik tombol “Tambahkan ke Koleksi” di bawah pratinjau kursor. Ini menambahkan kursor yang dipilih ke daftar pribadi Anda di dalam ekstensi.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Tambahkan ke koleksi">
  </div>

  <p>
    Setelah kursor ditambahkan, buka koleksi Anda di dalam ekstensi. Untuk <strong>menerapkan kursor</strong>, cukup klik di atasnya – dan itu akan langsung menjadi kursor aktif Anda.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Terapkan kursor">
  </div>
</div>
