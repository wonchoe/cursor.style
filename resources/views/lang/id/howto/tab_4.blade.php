
  <h2>Bagaimana cara menggunakan kursor di dalam ekstensi?</h2>
  <p>
    Setelah Anda menambahkan beberapa kursor ke daftar Anda menggunakan antarmuka ekstensi utama, Anda mungkin ingin mengelolanya.
    Menghapus kursor dari daftar aktif Anda itu mudah: cukup arahkan kursor ke kursor yang tidak lagi Anda butuhkan, dan klik
    <strong>ikon tempat sampah</strong> yang muncul di atasnya. Tindakan ini segera menghapus kursor dari
    daftar yang terlihat, menjaga ruang kerja Anda tetap bersih dan terorganisir.
  </p>

  <img src="/images/en/howto/delete.png" alt="Hapus kursor" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Jika Anda ingin mengubah efek yang terkait dengan kursor, cukup pilih dari pemilih dropdown dan klik
    tombol <strong>"Lihat opsi tambahan"</strong>. Di sana Anda dapat menyempurnakan tampilan dan perilaku efek agar lebih sesuai dengan gaya Anda.
  </p>

