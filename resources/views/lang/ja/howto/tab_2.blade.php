<div class="howto-step">
  <h2>コレクションにすばやくアクセスして管理</h2>

  <p>
    コレクションをすぐに開くには、ブラウザの右上隅にある<strong>拡張機能アイコンをクリック</strong>するだけです。これにより、個人のコレクションウィンドウに直接移動します。
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Open extension popup">
  </div>

  <p>
    <strong>コレクション</strong>タブ内で、ワンクリックで簡単に<strong>拡張機能を有効または無効にできます</strong>。これにより、必要に応じて、すぐにデフォルトのシステムカーソルに切り替えることができます。
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Enable or disable extension">
  </div>

  <p>
    カーソルの<strong>視覚効果を有効にすることもできます</strong>。効果スイッチを切り替えるだけで、選択した視覚的強化が即座に適用されます。
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Turn on effects">
  </div>
</div>
