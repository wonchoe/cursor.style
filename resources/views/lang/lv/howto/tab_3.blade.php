  <h2>Kā pielietot kursoru no savas kolekcijas</h2>

  <p>
    Lai izmantotu kursoru paplašinājumā, vispirms ir <strong>jāpievieno tas savai kolekcijai</strong>.
    To var izdarīt, noklikšķinot uz pogas “Pievienot kolekcijai” zem kursora priekšskatījuma. Tas pievieno izvēlēto kursoru jūsu personīgajam sarakstam paplašinājumā.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Pievienot kolekcijai">
  </div>

  <p>
    Kad kursors ir pievienots, atveriet savu kolekciju paplašinājumā. Lai <strong>pielietotu kursoru</strong>, vienkārši noklikšķiniet uz tā – un tas nekavējoties kļūs par jūsu aktīvo kursoru.
  </p>

