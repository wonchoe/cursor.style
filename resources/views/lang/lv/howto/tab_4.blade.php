
  <h2>Kā lietot kursorus paplašinājumā?</h2>
  <p>
    Pēc tam, kad esat pievienojis dažus kursorus savam sarakstam, izmantojot galveno paplašinājuma interfeisu, iespējams, vēlēsieties tos pārvaldīt.
    Kursa noņemšana no aktīvā saraksta ir vienkārša: vienkārši virziet kursoru uz kursora, kas jums vairs nav nepieciešams, un noklikšķiniet uz
    <strong>atkritumu tvertnes ikonas</strong>, kas parādās virs tā. Šī darbība nekavējoties izdzēš kursoru no jūsu
    redzamā saraksta, saglabājot jūsu darba vietu tīru un sakārtotu.
  </p>

  <img src="/images/en/howto/delete.png" alt="Dzēst kursoru" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Ja vēlaties mainīt efektu, kas saistīts ar kursoru, vienkārši izvēlieties to no nolaižamā saraksta un noklikšķiniet uz
    pogas <strong>"Skatīt papildu opcijas"</strong>. Tur varat precīzi noregulēt, kā efekts izskatās un darbojas, lai labāk atbilstu jūsu stilam.
  </p>

