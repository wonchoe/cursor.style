
  <h2>Cum se folosesc cursoarele în cadrul extensiei?</h2>
  <p>
    După ce ați adăugat câteva cursoare la lista dvs. folosind interfața principală a extensiei, este posibil să doriți să le gestionați.
    Eliminarea unui cursor din lista dvs. activă este simplă: doar treceți cu mouse-ul peste cursorul de care nu mai aveți nevoie și faceți clic pe
    <strong>iconița coșului de gunoi</strong> care apare deasupra acestuia. Această acțiune șterge imediat cursorul din lista dvs.
    vizibilă, menținând spațiul de lucru curat și organizat.
  </p>

  <img src="/images/en/howto/delete.png" alt="Ștergeți cursorul" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Dacă doriți să schimbați efectul asociat unui cursor, pur și simplu selectați-l din selectorul derulant și faceți clic pe
    butonul <strong>"Vizualizați opțiuni suplimentare"</strong>. Acolo puteți ajusta modul în care efectul arată și se comportă pentru a se potrivi mai bine stilului dvs.
  </p>

