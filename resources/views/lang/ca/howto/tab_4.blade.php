
  <h2>Com utilitzar els cursors dins l'extensió?</h2>
  <p>
    Després d'haver afegit uns quants cursors a la teva llista mitjançant la interfície principal de l'extensió, potser voldràs gestionar-los.
    Eliminar un cursor de la llista activa és senzill: simplement posa el cursor sobre el cursor que ja no necessites i fes clic a la 
    <strong>icona de la paperera</strong> que apareix a sobre. Aquesta acció suprimeix immediatament el cursor de la llista visible, mantenint el teu espai de treball net i organitzat.
  </p>

  <img src="/images/en/howto/delete.png" alt="Elimina el cursor" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Si vols canviar l'efecte associat a un cursor, simplement selecciona'l del selector desplegable i fes clic al botó 
    <strong>"Veure opcions addicionals"</strong>. Allà pots ajustar com es veu i es comporta l'efecte per adaptar-lo millor al teu estil.
  </p>

