<div class="howto-step">
  <h2>Brz pristup i upravljanje kolekcijom</h2>

  <p>
    Da biste odmah otvorili svoju kolekciju, samo <strong>kliknite na ikonu ekstenzije</strong> u gornjem desnom uglu vašeg pretraživača. Ovo će vas odvesti direktno u prozor vaše lične kolekcije.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Otvorite iskačući prozor ekstenzije">
  </div>

  <p>
    Unutar kartice <strong>Kolekcija</strong>, možete lako <strong>uključiti ili isključiti ekstenziju</strong> jednim klikom. Ovo vam pomaže da brzo prebacite nazad na podrazumevani sistemski kursor kada je potrebno.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Uključivanje ili isključivanje ekstenzije">
  </div>

  <p>
    Takođe možete <strong>omogućiti vizuelne efekte</strong> za vaš kursor. Samo prebacite prekidač efekta i vaše odabrano vizuelno poboljšanje će biti primenjeno odmah.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Uključivanje efekata">
  </div>
