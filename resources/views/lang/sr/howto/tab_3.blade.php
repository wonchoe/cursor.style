<div class="howto-step">
  <h2>Kako primeniti kursor iz vaše kolekcije</h2>

  <p>
    Da biste koristili kursor u ekstenziji, prvo morate da ga <strong>dodate u svoju kolekciju</strong>.
    To možete uraditi klikom na dugme "Dodaj u kolekciju" ispod pregleda kursora. Ovo dodaje izabrani kursor na vašu ličnu listu unutar ekstenzije.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Dodaj u kolekciju">
  </div>

  <p>
    Kada je kursor dodat, otvorite svoju kolekciju unutar ekstenzije. Da biste <strong>primenili kursor</strong>, samo kliknite na njega – i on će odmah postati vaš aktivni kursor.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Primeni kursor">
  </div>
