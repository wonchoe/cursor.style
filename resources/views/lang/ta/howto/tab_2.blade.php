<div>
  <h2>உங்கள் தொகுப்பை விரைவாக அணுகவும் மற்றும் நிர்வகிக்கவும்</h2>

  <p>
    உங்கள் சேகரிப்பை உடனடியாகத் திறக்க, உங்கள் உலாவியின் மேல் வலது மூலையில் உள்ள <strong>எக்ஸ்டென்ஷன் ஐகானைக் கிளிக் செய்யவும்</strong>. இது உங்களை நேரடியாக உங்கள் தனிப்பட்ட சேகரிப்பு சாளரத்திற்கு அழைத்துச் செல்லும்.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Open extension popup">
  </div>

  <p>
    <strong>சேகரிப்பு</strong> தாவலில், ஒரு கிளிக்கில் <strong>எக்ஸ்டென்ஷனை இயக்குதல் அல்லது முடக்குதல்</strong> ஆகியவற்றை எளிதாகச் செய்யலாம். தேவைப்படும்போது இயல்புநிலை கணினி கர்சருக்கு விரைவாக மாற இது உங்களுக்கு உதவுகிறது.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Enable or disable extension">
  </div>

  <p>
    உங்கள் கர்சருக்கான <strong>காட்சி விளைவுகளை இயக்கலாம்</strong>. விளைவு சுவிட்சை மட்டும் மாற்றவும், மேலும் உங்கள் தேர்ந்தெடுக்கப்பட்ட காட்சி மேம்பாடு உடனடியாகப் பயன்படுத்தப்படும்.
  </p>

