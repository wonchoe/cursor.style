<div class="howto-step">
  <h2>Kuinka ottaa kursori käyttöön kokoelmastasi</h2>

  <p>
    Käyttääksesi kursorin laajennuksessa, sinun on ensin <strong>lisättävä se kokoelmaasi</strong>.
    Voit tehdä tämän napsauttamalla "Lisää kokoelmaan" -painiketta kursorin esikatselun alla. Tämä lisää valitun kursorin henkilökohtaiseen luetteloosi laajennuksen sisällä.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Lisää kokoelmaan">
  </div>

  <p>
    Kun kursori on lisätty, avaa kokoelmasi laajennuksen sisällä. <strong>Ottaksesi kursorin käyttöön</strong>, napsauta sitä - ja siitä tulee aktiivinen kursorisi välittömästi.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Ota kursori käyttöön">
  </div>
</div>
