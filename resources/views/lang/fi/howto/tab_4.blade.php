<h2>Kuinka käyttää kursoreita laajennuksen sisällä?</h2>
  <p>
    Kun olet lisännyt luetteloon muutamia kursoreita pääkäyttöliittymän avulla, saatat haluta hallita niitä.
    Kursorin poistaminen aktiivisesta luettelostasi on yksinkertaista: vie hiiri kursorin päälle, jota et enää tarvitse, ja napsauta
    sen päällä näkyvää <strong>roskakori-ikonia</strong>. Tämä toiminto poistaa kursorin välittömästi näkyvältä luetteloltasi ja pitää työtilasi siistinä ja järjestyksessä.
  </p>

  <img src="/images/fi/howto/delete.png" alt="Poista kursori" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Jos haluat muuttaa kursorin yhteydessä olevaa tehostetta, valitse se avattavasta valitsimesta ja napsauta
    <strong>"Katso lisäasetukset"</strong> -painiketta. Siellä voit hienosäätää, miltä tehoste näyttää ja käyttäytyy, sopiakseen paremmin tyyliisi.
  </p>

