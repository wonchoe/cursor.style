<h2>如何在 "Cursor Style" 扩展程序中使用光标？</h2>
  <p>
    在您使用主扩展程序界面将一些光标添加到您的列表中后，您可能想要管理它们。
    从您的活动列表中删除光标很简单：只需将鼠标悬停在您不再需要的光标上，然后单击
    其顶部出现的<strong>垃圾箱图标</strong>。此操作会立即从您的可见列表中删除光标，保持您的工作区干净整洁。
  </p>

  <img src="/images/en/howto/delete.png" alt="删除光标" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    如果您想更改与光标关联的效果，只需从下拉选择器中选择它，然后单击
    <strong>"查看其他选项"</strong> 按钮。在那里您可以微调效果的外观和行为，以更好地适应您的风格。
  </p>

