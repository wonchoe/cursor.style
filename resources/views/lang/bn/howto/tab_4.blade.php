  <h2>Extension-এর ভিতরে কার্সার কীভাবে ব্যবহার করবেন?</h2>
  <p>
    আপনি প্রধান extension ইন্টারফেস ব্যবহার করে আপনার তালিকায় কয়েকটি কার্সার যোগ করার পরে, আপনি সেগুলিকে পরিচালনা করতে চাইতে পারেন।
    আপনার সক্রিয় তালিকা থেকে একটি কার্সার সরানো সহজ: আপনার যে কার্সারটি আর দরকার নেই, তার উপর কেবল আপনার মাউস ধরুন এবং
    এর উপরে প্রদর্শিত <strong>ট্র্যাশ বিন আইকনটিতে</strong> ক্লিক করুন। এই কাজটি অবিলম্বে আপনার দৃশ্যমান তালিকা থেকে কার্সারটি মুছে দেয়, যা আপনার
    কাজের স্থানটিকে পরিষ্কার এবং সুসংগঠিত রাখে।
  </p>

  <img src="/images/en/howto/delete.png" alt="কার্সার মুছুন" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    আপনি যদি কোনও কার্সারের সাথে যুক্ত প্রভাব পরিবর্তন করতে চান তবে কেবল ড্রপডাউন নির্বাচক থেকে এটি নির্বাচন করুন এবং
    <strong>"অতিরিক্ত বিকল্পগুলি দেখুন"</strong> বোতামটিতে ক্লিক করুন। সেখানে আপনি আপনার শৈলীর সাথে আরও ভালভাবে মানানসই করতে প্রভাবটি কীভাবে দেখায় এবং আচরণ করে তা সূক্ষ্মভাবে দেখতে পারেন।
  </p>

