<div class="howto-step">
  <h2>Como aplicar um cursor da sua coleção</h2>

  <p>
    Para usar um cursor na extensão, primeiro precisa de <strong>adicioná-lo à sua coleção</strong>.
    Pode fazer isto clicando no botão “Adicionar à Coleção” sob a pré-visualização do cursor. Isto adiciona o cursor selecionado à sua lista pessoal dentro da extensão.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Adicionar à coleção">
  </div>

  <p>
    Depois do cursor ser adicionado, abra a sua coleção dentro da extensão. Para <strong>aplicar o cursor</strong>, basta clicar nele – e ele tornar-se-á o seu cursor ativo instantaneamente.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Aplicar cursor">
  </div>
</div>
