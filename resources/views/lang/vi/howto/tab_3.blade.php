<div class="howto-step">
  <h2>Cách áp dụng một con trỏ từ bộ sưu tập của bạn</h2>

  <p>
    Để sử dụng một con trỏ trong tiện ích mở rộng, trước tiên bạn cần <strong>thêm nó vào bộ sưu tập của mình</strong>.
    Bạn có thể thực hiện việc này bằng cách nhấp vào nút “Thêm vào Bộ sưu tập” bên dưới bản xem trước con trỏ. Thao tác này thêm con trỏ đã chọn vào danh sách cá nhân của bạn bên trong tiện ích mở rộng.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Thêm vào bộ sưu tập">
  </div>

  <p>
    Sau khi con trỏ đã được thêm, hãy mở bộ sưu tập của bạn bên trong tiện ích mở rộng. Để <strong>áp dụng con trỏ</strong>, chỉ cần nhấp vào nó – và nó sẽ trở thành con trỏ đang hoạt động của bạn ngay lập tức.
  </p>

