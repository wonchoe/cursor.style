  <h2>Kaip pritaikyti žymeklį iš savo kolekcijos</h2>

  <p>
    Norėdami naudoti žymeklį plėtinyje, pirmiausia turite jį <strong>pridėti prie savo kolekcijos</strong>.
    Tai galite padaryti spustelėję mygtuką „Pridėti į kolekciją“ po žymeklio peržiūra. Tai pasirinktą žymeklį įtraukia į jūsų asmeninį sąrašą plėtinyje.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Pridėti į kolekciją">
  </div>

  <p>
    Pridėjus žymeklį, atidarykite savo kolekciją plėtinyje. Norėdami <strong>pritaikyti žymeklį</strong>, tiesiog spustelėkite jį – ir jis iškart taps jūsų aktyviuoju žymekliu.
  </p>

