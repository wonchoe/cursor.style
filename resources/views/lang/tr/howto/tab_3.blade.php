  <h2>Koleksiyonunuzdan bir imleç nasıl uygulanır</h2>

  <p>
    Uzantıda bir imleç kullanmak için, önce onu <strong>koleksiyonunuza eklemeniz</strong> gerekir.
    Bunu, imleç önizlemesinin altındaki “Koleksiyona Ekle” düğmesine tıklayarak yapabilirsiniz. Bu, seçilen imleci uzantı içindeki kişisel listenize ekler.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Koleksiyona ekle">
  </div>

  <p>
    İmleç eklendikten sonra, uzantı içindeki koleksiyonunuzu açın. <strong>İmleci uygulamak</strong> için, sadece üzerine tıklayın – ve anında aktif imleciniz olacaktır.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="İmleci uygula">
  </div>
</div>
