<div class="howto-step">
  <h2>Slik bruker du en markør fra samlingen din</h2>

  <p>
    For å bruke en markør i utvidelsen, må du først <strong>legge den til i samlingen din</strong>.
    Du kan gjøre dette ved å klikke på "Legg til i samling"-knappen under markørforhåndsvisningen. Dette legger den valgte markøren til din personlige liste inne i utvidelsen.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Legg til i samling">
  </div>

  <p>
    Når markøren er lagt til, åpner du samlingen din inne i utvidelsen. For å <strong>bruke markøren</strong>, bare klikk på den – og den vil umiddelbart bli din aktive markør.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Bruk markør">
  </div>
</div>
