
  <h2>Hvordan bruke markører i utvidelsen?</h2>
  <p>
    Etter at du har lagt til noen markører i listen din ved hjelp av hovedgrensesnittet for utvidelsen, kan du kanskje administrere dem.
    Å fjerne en markør fra den aktive listen din er enkelt: bare hold musepekeren over markøren du ikke lenger trenger, og klikk på
    <strong>søppelbøtteikonet</strong> som vises på toppen av den. Denne handlingen sletter umiddelbart markøren fra din
    synlige liste, og holder arbeidsområdet ditt rent og organisert.
  </p>

  <img src="/images/en/howto/delete.png" alt="Slett markør" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Hvis du vil endre effekten som er knyttet til en markør, velger du den ganske enkelt fra rullegardinmenyen og klikker på
    knappen <strong>"Vis flere alternativer"</strong>. Der kan du finjustere hvordan effekten ser ut og oppfører seg for å bedre passe din stil.
  </p>

