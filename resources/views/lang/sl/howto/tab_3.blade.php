  <h2>Kako uporabiti kazalec iz svoje zbirke</h2>

  <p>
    Za uporabo kazalca v razširitvi ga morate najprej <strong>dodati v svojo zbirko</strong>.
    To storite tako, da kliknete gumb “Dodaj v zbirko” pod predogledom kazalca. To doda izbrani kazalec na vaš osebni seznam v razširitvi.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Dodaj v zbirko">
  </div>

  <p>
    Ko je kazalec dodan, odprite svojo zbirko v razširitvi. Za <strong>uporabo kazalca</strong> preprosto kliknite nanj – in takoj bo postal vaš aktivni kazalec.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Uporabi kazalec">
  </div>
