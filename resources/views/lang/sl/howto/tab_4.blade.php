<h2>Kako uporabljati kurzorje znotraj razširitve?</h2>
  <p>
    Ko dodate nekaj kurzorjev na svoj seznam z uporabo glavnega vmesnika razširitve, jih boste morda želeli upravljati.
    Odstranjevanje kurzorja z vašega aktivnega seznama je preprosto: samo premaknite miško nad kurzor, ki ga ne potrebujete več, in kliknite na
    <strong>ikono koša za smeti</strong>, ki se prikaže na vrhu. Ta dejanje takoj izbriše kurzor z vašega
    vidnega seznama, s čimer ohranja vaše delovno območje čisto in urejeno.
  </p>

  <img src="/images/en/howto/delete.png" alt="Izbriši kurzor" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Če želite spremeniti učinek, povezan s kurzorjem, ga preprosto izberite iz spustnega izbirnika in kliknite
    gumb <strong>"Prikaži dodatne možnosti"</strong>. Tam lahko natančno nastavite, kako izgleda in se obnaša učinek, da bo bolj ustrezal vašemu slogu.
  </p>

