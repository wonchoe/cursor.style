<div class="howto-step">
  <h2>الوصول إلى مجموعتك وإدارتها بسرعة</h2>

  <p>
    لفتح مجموعتك على الفور، ما عليك سوى <strong>النقر فوق أيقونة الإضافة</strong> في الزاوية العلوية اليمنى من متصفحك. سينقلك هذا مباشرة إلى نافذة مجموعتك الشخصية.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Open extension popup">
  </div>

  <p>
    داخل علامة التبويب <strong>Collection</strong>، يمكنك بسهولة <strong>تمكين أو تعطيل الإضافة</strong> بنقرة واحدة. هذا يساعدك على العودة بسرعة إلى مؤشر النظام الافتراضي عند الحاجة.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Enable or disable extension">
  </div>

  <p>
    يمكنك أيضًا <strong>تمكين التأثيرات المرئية</strong> لمؤشرك. ما عليك سوى تبديل مفتاح التأثير، وسيتم تطبيق التحسين المرئي المحدد على الفور.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Turn on effects">
  </div>
</div>