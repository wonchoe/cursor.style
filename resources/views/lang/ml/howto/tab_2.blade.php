  <h2>നിങ്ങളുടെ ശേഖരം വേഗത്തിൽ ആക്സസ് ചെയ്യുക, നിയന്ത്രിക്കുക</h2>

  <p>
    നിങ്ങളുടെ ശേഖരം തൽക്ഷണം തുറക്കാൻ, ബ്രൗസറിന്റെ മുകളിൽ വലത് കോണിലുള്ള <strong>എക്സ്റ്റൻഷൻ ഐക്കണിൽ ക്ലിക്കുചെയ്യുക</strong>. ഇത് നിങ്ങളുടെ വ്യക്തിഗത ശേഖരണ വിൻഡോയിലേക്ക് നേരിട്ട് കൊണ്ടുപോകും.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Open extension popup">
  </div>

  <p>
    <strong>Collection</strong> ടാബിനുള്ളിൽ, നിങ്ങൾക്ക് ഒരു ക്ലിക്കിലൂടെ തന്നെ <strong>എക്സ്റ്റൻഷൻ പ്രവർത്തനക്ഷമമാക്കാനും പ്രവർത്തനരഹിതമാക്കാനും</strong> കഴിയും. ആവശ്യമുള്ളപ്പോൾ ഡിഫോൾട്ട് സിസ്റ്റം കേർസറിലേക്ക് വേഗത്തിൽ മാറാൻ ഇത് നിങ്ങളെ സഹായിക്കുന്നു.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Enable or disable extension">
  </div>

  <p>
    നിങ്ങൾക്ക് കേർസറിനായി <strong>വിഷ്വൽ ഇഫക്റ്റുകൾ പ്രവർത്തനക്ഷമമാക്കാനും</strong> കഴിയും. ഇഫക്റ്റ് സ്വിച്ച് ടോഗിൾ ചെയ്യുക, നിങ്ങളുടെ തിരഞ്ഞെടുത്ത വിഷ്വൽ എൻഹാൻസ്മെന്റ് തൽക്ഷണം ബാധകമാകും.
  </p>

