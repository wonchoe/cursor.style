<div class="howto-step">
  <h2>Kom åt och hantera din samling snabbt</h2>

  <p>
    För att omedelbart öppna din samling, klicka bara på <strong>tilläggsikonen</strong> i det övre högra hörnet av din webbläsare. Detta tar dig direkt till ditt personliga samlingsfönster.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Öppna tilläggspopup">
  </div>

  <p>
    I fliken <strong>Samling</strong> kan du enkelt <strong>aktivera eller inaktivera tillägget</strong> med ett klick. Detta hjälper dig att snabbt byta tillbaka till den vanliga systemmarkören när det behövs.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Aktivera eller inaktivera tillägg">
  </div>

  <p>
    Du kan också <strong>aktivera visuella effekter</strong> för din markör. Växla bara effektomkopplaren, så kommer din valda visuella förbättring att tillämpas omedelbart.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Slå på effekter">
  </div>
</div>
