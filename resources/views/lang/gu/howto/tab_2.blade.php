<div class="howto-step">
  <h2>તમારી કલેક્શનને ઝડપથી ઍક્સેસ કરો અને મેનેજ કરો</h2>

  <p>
    તમારી કલેક્શનને તરત જ ખોલવા માટે, ફક્ત તમારા બ્રાઉઝરના ઉપર-જમણા ખૂણામાં <strong>એક્સટેન્શન આઇકન પર ક્લિક કરો</strong>. આ તમને સીધા જ તમારી વ્યક્તિગત કલેક્શન વિંડો પર લઈ જશે.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Open extension popup">
  </div>

  <p>
    <strong>કલેક્શન</strong> ટેબની અંદર, તમે એક જ ક્લિકથી સરળતાથી <strong>એક્સટેન્શનને સક્ષમ અથવા અક્ષમ કરી શકો છો</strong>. જ્યારે જરૂર હોય ત્યારે આ તમને ઝડપથી ડિફોલ્ટ સિસ્ટમ કર્સર પર સ્વિચ કરવામાં મદદ કરે છે.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Enable or disable extension">
  </div>

  <p>
    તમે તમારા કર્સર માટે <strong>વિઝ્યુઅલ ઇફેક્ટ્સ પણ સક્ષમ કરી શકો છો</strong>. ફક્ત ઇફેક્ટ સ્વિચને ટૉગલ કરો, અને તમારું પસંદ કરેલું વિઝ્યુઅલ એન્હેન્સમેન્ટ તરત જ લાગુ કરવામાં આવશે.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Turn on effects">
  </div>
</div>
