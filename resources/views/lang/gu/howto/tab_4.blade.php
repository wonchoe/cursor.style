
  <h2>એક્સ્ટેંશનની અંદર કર્સર્સનો ઉપયોગ કેવી રીતે કરવો?</h2>
  <p>
    મુખ્ય એક્સ્ટેંશન ઇન્ટરફેસનો ઉપયોગ કરીને તમે તમારી સૂચિમાં થોડા કર્સર્સ ઉમેર્યા પછી, તમે તેને મેનેજ કરવા માંગી શકો છો.
    તમારી સક્રિય સૂચિમાંથી કર્સરને દૂર કરવું સરળ છે: ફક્ત તમને હવે જરૂર ન હોય તેવા કર્સર પર હોવર કરો અને
    તેની ટોચ પર દેખાતા <strong>ટ્રેશ બિન આઇકોન</strong> પર ક્લિક કરો. આ ક્રિયા તરત જ તમારા કર્સરને તમારી
    દૃશ્યમાન સૂચિમાંથી કાઢી નાખે છે, તમારા કાર્યસ્થળને સ્વચ્છ અને વ્યવસ્થિત રાખે છે.
  </p>

  <img src="/images/en/howto/delete.png" alt="Delete cursor" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    જો તમે કર્સર સાથે સંકળાયેલ અસર બદલવા માંગો છો, તો ફક્ત તેને ડ્રોપડાઉન સિલેક્ટરમાંથી પસંદ કરો અને
    <strong>"વધારાના વિકલ્પો જુઓ"</strong> બટન પર ક્લિક કરો. ત્યાં તમે તમારી શૈલીને વધુ સારી રીતે અનુકૂળ બનાવવા માટે અસર કેવી દેખાય છે અને વર્તે છે તેને ઝીણવટપૂર્વક સુધારી શકો છો.
  </p>

