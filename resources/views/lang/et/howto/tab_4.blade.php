<h2>Kuidas kursorit laienduses kasutada?</h2>
  <p>
    Pärast seda, kui olete põhiliidese abil oma loendisse mõned kursorid lisanud, võiksite neid hallata.
    Kursori eemaldamine aktiivsest loendist on lihtne: viige kursorile, mida te enam ei vaja, ja klõpsake nuppu
    <strong>prügikasti ikooni</strong>, mis ilmub selle kohale. See toiming kustutab kursori kohe teie
    nähtavast loendist, hoides teie tööruumi puhta ja korrastatuna.
  </p>

  <img src="/images/en/howto/delete.png" alt="Kustuta kursor" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Kui soovite muuta kursoriga seotud efekti, valige see lihtsalt rippmenüüst ja klõpsake
    nuppu <strong>"Vaata lisavalikuid"</strong>. Seal saate täpsustada, kuidas efekt välja näeb ja käitub, et see paremini teie stiiliga sobiks.
  </p>

