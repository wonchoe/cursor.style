
  <h2>Hoe cursors binnen de extensie te gebruiken?</h2>
  <p>
    Nadat je een paar cursors aan je lijst hebt toegevoegd met behulp van de belangrijkste extensie-interface, wil je ze misschien beheren.
    Het verwijderen van een cursor uit je actieve lijst is eenvoudig: zweef gewoon met de muis over de cursor die je niet langer nodig hebt en klik op het
    <strong>prullenbakpictogram</strong> dat erboven verschijnt. Deze actie verwijdert de cursor onmiddellijk uit je
    zichtbare lijst, waardoor je werkruimte schoon en georganiseerd blijft.
  </p>

  <img src="/images/en/howto/delete.png" alt="Cursor verwijderen" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Als je het effect dat aan een cursor is gekoppeld wilt wijzigen, selecteer je deze gewoon uit de vervolgkeuzelijst en klik je op
    de <strong>"Bekijk extra opties"</strong>-knop. Daar kun je de fijnafstelling van het uiterlijk en het gedrag van het effect verfijnen om beter bij je stijl te passen.
  </p>

