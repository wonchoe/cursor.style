  <h2>Hoe u een cursor uit uw verzameling toepast</h2>

  <p>
    Om een cursor in de extensie te gebruiken, moet u deze eerst aan <strong>uw verzameling toevoegen</strong>.
    U kunt dit doen door op de knop “Toevoegen aan verzameling” onder de cursorvoorbeeld te klikken. Dit voegt de geselecteerde cursor toe aan uw persoonlijke lijst binnen de extensie.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Toevoegen aan verzameling">
  </div>

  <p>
    Zodra de cursor is toegevoegd, opent u uw verzameling binnen de extensie. Om de <strong>cursor toe te passen</strong>, klikt u er gewoon op – en hij wordt direct uw actieve cursor.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Cursor toepassen">
  </div>
</div>
