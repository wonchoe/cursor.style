<h2>Hogyan használhatók a kurzorok a bővítményen belül?</h2>
  <p>
    Miután hozzáadott néhány kurzort a listájához a fő bővítmény felületén, érdemes lehet kezelni őket.
    Egy kurzor eltávolítása az aktív listájából egyszerű: csak vigye az egérmutatót a már nem kívánt kurzor fölé, és kattintson a
    <strong>szemeteskuka ikonra</strong>, amely a tetején jelenik meg. Ez a művelet azonnal törli a kurzort a
    látható listájából, tisztán és rendezetten tartva a munkaterületét.
  </p>

  <img src="/images/en/howto/delete.png" alt="Kurzor törlése" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Ha meg szeretné változtatni a kurzorhoz társított effektust, egyszerűen válassza ki azt a legördülő menüből, és kattintson a
    <strong>"További opciók megtekintése"</strong> gombra. Itt finomhangolhatja az effektus kinézetét és viselkedését, hogy jobban illeszkedjen a stílusához.
  </p>

