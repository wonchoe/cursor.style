<div class="howto-step">
  <h2>Gyorsan elérheti és kezelheti a gyűjteményét</h2>

  <p>
    A gyűjtemény azonnali megnyitásához egyszerűen <strong>kattintson a bővítmény ikonjára</strong> a böngésző jobb felső sarkában. Ez közvetlenül a személyes gyűjteményablakba viszi.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Nyissa meg a bővítmény felugró ablakát">
  </div>

  <p>
    A <strong>Gyűjtemény</strong> lapon könnyedén <strong>engedélyezheti vagy letilthatja a bővítményt</strong> egyetlen kattintással. Ez segít gyorsan visszatérni az alapértelmezett rendszerkurzorhoz, amikor szükséges.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Bővítmény engedélyezése vagy letiltása">
  </div>

  <p>
    A kurzorhoz <strong>vizuális effektusokat is engedélyezhet</strong>. Csak kapcsolja be az effektus kapcsolót, és a kiválasztott vizuális javítás azonnal alkalmazásra kerül.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Effektusok bekapcsolása">
  </div>
</div>
