<div class="howto-step">
  <h2>Jak zastosować kursor z twojej kolekcji</h2>

  <p>
    Aby użyć kursora w rozszerzeniu, musisz najpierw <strong>dodać go do swojej kolekcji</strong>.
    Możesz to zrobić, klikając przycisk „Dodaj do kolekcji” pod podglądem kursora. Spowoduje to dodanie wybranego kursora do twojej osobistej listy wewnątrz rozszerzenia.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Dodaj do kolekcji">
  </div>

  <p>
    Po dodaniu kursora, otwórz swoją kolekcję wewnątrz rozszerzenia. Aby <strong>zastosować kursor</strong>, po prostu kliknij na niego – a natychmiast stanie się twoim aktywnym kursorem.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Zastosuj kursor">
  </div>
</div>
