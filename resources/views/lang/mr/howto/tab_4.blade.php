
  <h2>कर्सर विस्तारामध्ये कसे वापरावे?</h2>
  <p>
    मुख्य विस्तार इंटरफेस वापरून तुम्ही तुमच्या सूचीमध्ये काही कर्सर जोडल्यानंतर, तुम्हाला त्याचे व्यवस्थापन करायचे असेल.
    तुमच्या सक्रिय सूचीमधून कर्सर काढणे सोपे आहे: तुम्हाला यापुढे नको असलेल्या कर्सरवर फक्त कर्सर फिरवा, आणि त्यावर
    देणाऱ्या <strong>कचरापेटी चिन्हावर</strong> क्लिक करा. हे कृत्य त्वरित तुमच्या
    दृश्यमान सूचीमधून कर्सर हटवते, तुमचे कार्यस्थान स्वच्छ आणि व्यवस्थित ठेवते.
  </p>

  <img src="/images/en/howto/delete.png" alt="कर्सर हटवा" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    तुम्हाला कर्सरशी संबंधित प्रभाव बदलायचा असल्यास, तो ड्रॉपडाउन निवडकातून निवडा आणि
    <strong>"अतिरिक्त पर्याय पहा"</strong> बटणावर क्लिक करा. तिथे तुम्ही तुमच्या शैलीला अधिक अनुरूप होण्यासाठी प्रभावाचे स्वरूप आणि वर्तन बारीक करू शकता.
  </p>

