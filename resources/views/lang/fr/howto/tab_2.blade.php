<div class="howto-step">
  <h2>Accédez à votre collection et gérez-la rapidement</h2>

  <p>
    Pour ouvrir instantanément votre collection, il vous suffit de <strong>cliquer sur l'icône de l'extension</strong> dans le coin supérieur droit de votre navigateur. Cela vous mènera directement à votre fenêtre de collection personnelle.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/presspopup.png" alt="Ouvrir la fenêtre contextuelle de l'extension">
  </div>

  <p>
    Dans l'onglet <strong>Collection</strong>, vous pouvez facilement <strong>activer ou désactiver l'extension</strong> en un seul clic. Cela vous aide à revenir rapidement au curseur système par défaut en cas de besoin.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnon.png" alt="Activer ou désactiver l'extension">
  </div>

  <p>
    Vous pouvez également <strong>activer des effets visuels</strong> pour votre curseur. Il suffit d'activer l'interrupteur d'effet, et votre amélioration visuelle sélectionnée sera appliquée instantanément.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/turnoneffect.png" alt="Activer les effets">
  </div>
</div>
