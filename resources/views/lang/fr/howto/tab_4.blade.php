<h2>Comment utiliser les curseurs dans l'extension ?</h2>
  <p>
    Une fois que vous avez ajouté quelques curseurs à votre liste via l'interface principale de l'extension, vous souhaiterez peut-être les gérer.
    Supprimer un curseur de votre liste active est simple : survolez le curseur dont vous n'avez plus besoin et cliquez sur l'icône de la
    <strong>corbeille</strong> qui apparaît en haut. Cette action supprime immédiatement le curseur de votre
    liste visible, en gardant votre espace de travail propre et organisé.
  </p>

  <img src="/images/fr/howto/delete.png" alt="Supprimer le curseur" style="max-width: 100%; border-radius: 10px; margin: 20px 0;" />

  <p>
    Si vous souhaitez modifier l'effet associé à un curseur, sélectionnez-le simplement dans le sélecteur déroulant et cliquez sur le bouton
    <strong>"Voir les options supplémentaires"</strong>. Vous pourrez alors affiner l'apparence et le comportement de l'effet pour qu'il corresponde mieux à votre style.
  </p>

