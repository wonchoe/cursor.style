<div class="howto-step">
  <h2>Comment appliquer un curseur de votre collection</h2>

  <p>
    Pour utiliser un curseur dans l'extension, vous devez d'abord <strong>l'ajouter à votre collection</strong>.
    Vous pouvez le faire en cliquant sur le bouton “Ajouter à la collection” sous l'aperçu du curseur. Cela ajoute le curseur sélectionné à votre liste personnelle dans l'extension.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/addtocollection.png" alt="Ajouter à la collection">
  </div>

  <p>
    Une fois le curseur ajouté, ouvrez votre collection dans l'extension. Pour <strong>appliquer le curseur</strong>, cliquez simplement dessus – et il deviendra instantanément votre curseur actif.
  </p>

  <div class="howto-img">
    <img src="/images/en/howto/applycursor.png" alt="Appliquer le curseur">
  </div>
</div>
